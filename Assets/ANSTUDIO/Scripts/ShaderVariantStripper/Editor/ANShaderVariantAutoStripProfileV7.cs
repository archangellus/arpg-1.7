    public class ANShaderVariantAutoStripProfileV7 : ScriptableObject
    {
        [Header("Build Stripper")]
        public bool enableBuildStripper = false;

        [Tooltip("If enabled, the automatic stripper will only process shaders whose names look like URP shaders. Disable this if your custom shaders use the same keywords and you want them stripped too.")]
        public bool limitToURPShaders = false;

        [Tooltip("If enabled, the stripper will log shader snippets where variants were removed.")]
        public bool verboseBuildLog = false;

        [Tooltip("If enabled, shader name blocklist entries are treated as regular expressions first, then as plain substring fallbacks if a regex is invalid.")]
        public bool useRegexShaderNameBlocklist = true;

        [Header("Generated From Scanner")]
        [Tooltip("Generated by the scanner. Every build variant containing one of these keywords will be removed.")]
        public List<string> stripKeywords = new List<string>();

        [Tooltip("Keywords that were considered risky or need manual review.")]
        public List<string> reviewKeywords = new List<string>();

        [Tooltip("Human-readable notes generated by the scanner.")]
        public List<string> notes = new List<string>();

        [Tooltip("Hash of the recommended strip-keyword list that generated this profile. Used to warn if the project changed since profile generation.")]
        public string profileVersionHash = "";

        [Tooltip("Build target that was selected when this profile was generated.")]
        public string generatedForBuildTarget = "";

        [Header("Safety")]
        [Tooltip("Do not strip if this would remove every variant from a shader snippet.")]
        public bool keepAtLeastOneVariantPerSnippet = true;

        [Tooltip("Never strip a compiler variant that appears to have no enabled keywords. This is an extra guard for the base fallback variant.")]
        public bool neverStripBaseVariant = true;

        [Tooltip("Shaders containing one of these strings or regex patterns are never stripped.")]
        public List<string> shaderNameBlocklist = new List<string>
        {
            "Hidden/Internal",
            "Hidden/Blit",
            "Hidden/Universal Render Pipeline/CoreBlit",
            "TextMeshPro",
            "Sprites/"
        };

        public void Sanitize()
        {
            stripKeywords = SanitizeKeywordList(stripKeywords);
            reviewKeywords = SanitizeKeywordList(reviewKeywords);
            notes = notes == null ? new List<string>() : notes.Where(n => !string.IsNullOrWhiteSpace(n)).Distinct().ToList();
            shaderNameBlocklist = shaderNameBlocklist == null ? new List<string>() : shaderNameBlocklist.Where(n => !string.IsNullOrWhiteSpace(n)).Distinct().ToList();
        }

        private static List<string> SanitizeKeywordList(IEnumerable<string> source)
        {
            if (source == null)
                return new List<string>();

            return source
                .Where(k => !string.IsNullOrWhiteSpace(k))
                .Select(k => k.Trim())
                .Where(k => Regex.IsMatch(k, "^[A-Za-z0-9_]+$"))
                .Distinct(StringComparer.Ordinal)
                .OrderBy(k => k, StringComparer.Ordinal)
                .ToList();
        }
    }

    public sealed class ANAutoShaderVariantStripperFromScannerV7 : IPreprocessShaders, IPreprocessBuildWithReport, IPostprocessBuildWithReport
    {
        private const string ProfileTypeName = "ANShaderVariantAutoStripProfileV7";
        private const string PersistedReportPath = "ProjectSettings/ANSTUDIO_ShaderVariantUsageScanner_LastScanV7.json";
        private static ANShaderVariantAutoStripProfileV7 cachedProfile;
        private static string cachedProfilePath;
        private static readonly Dictionary<string, ShaderKeyword> globalKeywordCache = new Dictionary<string, ShaderKeyword>(StringComparer.Ordinal);

        public int callbackOrder => 10000;


        public void OnPreprocessBuild(BuildReport report)
        {
            globalKeywordCache.Clear();
            cachedProfile = null;
            cachedProfilePath = null;
            WarnIfProfileHashLooksOutOfDate();
        }

        public void OnPostprocessBuild(BuildReport report)
        {
            try
            {
                SavePostBuildReport(report);
            }
            catch (Exception ex)
            {
                Debug.LogWarning("[AN Shader Variant Scanner V7] Failed to save post-build shader report: " + ex.Message);
            }
        }

        private static void SavePostBuildReport(BuildReport report)
        {
            if (report == null)
                return;

            string folder = Path.Combine(Directory.GetCurrentDirectory(), "ProjectSettings/ANSTUDIO_ShaderVariantUsageScanner_HistoryV7");
            if (!Directory.Exists(folder))
                Directory.CreateDirectory(folder);

            var files = report.GetFiles();
            List<string> shaderFiles = new List<string>();
            long shaderFileBytes = 0;

            if (files != null)
            {
                foreach (object file in files.Cast<object>())
                {
                    string path = ReadStringMember(file, "path");
                    string role = ReadStringMember(file, "role");
                    long size = ReadLongMember(file, "size");

                    string combined = (path + " " + role).ToLowerInvariant();
                    if (combined.Contains("shader") || combined.Contains("variant") || combined.Contains("globalgamemanagers"))
                    {
                        shaderFileBytes += Math.Max(0, size);
                        shaderFiles.Add(path + " | role: " + role + " | bytes: " + size);
                    }
                }
            }

            long lastBudgetTotal = EstimateLastBudgetTotalFromPersistedReport();

            StringBuilder sb = new StringBuilder();
            sb.AppendLine("{");
            sb.AppendLine("  \"postBuildTime\": " + JsonEscape(DateTime.Now.ToString("yyyy-MM-dd HH:mm:ss")) + ",");
            sb.AppendLine("  \"buildTarget\": " + JsonEscape(report.summary.platform.ToString()) + ",");
            sb.AppendLine("  \"outputPath\": " + JsonEscape(report.summary.outputPath ?? "") + ",");
            sb.AppendLine("  \"result\": " + JsonEscape(report.summary.result.ToString()) + ",");
            sb.AppendLine("  \"totalSize\": " + report.summary.totalSize + ",");
            sb.AppendLine("  \"shaderRelatedFileBytes\": " + shaderFileBytes + ",");
            sb.AppendLine("  \"lastKnownEstimatedVariantBudgetTotal\": " + lastBudgetTotal + ",");
            sb.AppendLine("  \"shaderRelatedFiles\": [");
            for (int i = 0; i < shaderFiles.Count; i++)
                sb.AppendLine("    " + JsonEscape(shaderFiles[i]) + (i + 1 < shaderFiles.Count ? "," : ""));
            sb.AppendLine("  ]");
            sb.AppendLine("}");

            string stamp = DateTime.Now.ToString("yyyyMMdd_HHmmss");
            string pathOut = Path.Combine(folder, "AN_ShaderVariantUsageScanner_PostBuild_" + stamp + ".json");
            File.WriteAllText(pathOut, sb.ToString(), Encoding.UTF8);
            Debug.Log("[AN Shader Variant Scanner V7] Post-build shader report saved: " + pathOut);
        }

        private static long EstimateLastBudgetTotalFromPersistedReport()
        {
            try
            {
                if (!File.Exists(PersistedReportPath))
                    return -1;

                string json = File.ReadAllText(PersistedReportPath, Encoding.UTF8);
                MatchCollection matches = Regex.Matches(json, "\"estimatedVariantCount\"\\s*:\\s*(-?\\d+)");
                long total = 0;
                int count = 0;

                foreach (Match match in matches)
                {
                    long value;
                    if (long.TryParse(match.Groups[1].Value, out value) && value > 0)
                    {
                        total += value;
                        count++;
                    }
                }

                return count == 0 ? -1 : total;
            }
            catch
            {
                return -1;
            }
        }

        private static string ReadStringMember(object source, string memberName)
        {
            if (source == null)
                return "";

            Type type = source.GetType();
            FieldInfo field = type.GetField(memberName, BindingFlags.Public | BindingFlags.Instance);
            if (field != null)
            {
                object value = field.GetValue(source);
                return value == null ? "" : value.ToString();
            }

            PropertyInfo property = type.GetProperty(memberName, BindingFlags.Public | BindingFlags.Instance);
            if (property != null)
            {
                object value = property.GetValue(source, null);
                return value == null ? "" : value.ToString();
            }

            return "";
        }

        private static long ReadLongMember(object source, string memberName)
        {
            string value = ReadStringMember(source, memberName);
            long parsed;
            return long.TryParse(value, out parsed) ? parsed : 0;
        }

        private static string JsonEscape(string value)
        {
            if (value == null)
                value = "";

            return "\"" + value.Replace("\\", "\\\\").Replace("\"", "\\\"").Replace("\r", "\\r").Replace("\n", "\\n") + "\"";
        }

        public void OnProcessShader(Shader shader, ShaderSnippetData snippet, IList<ShaderCompilerData> data)
        {
            ANShaderVariantAutoStripProfileV7 profile = GetProfile();
            if (profile == null || !profile.enableBuildStripper || profile.stripKeywords == null || profile.stripKeywords.Count == 0)
                return;

            if (shader == null || data == null || data.Count == 0)
                return;

            string shaderName = shader.name ?? string.Empty;

            if (profile.limitToURPShaders && !LooksLikeURPShader(shaderName))
                return;

            if (IsShaderBlocked(shaderName, profile))
                return;

            int before = data.Count;
            int removed = 0;

            for (int i = data.Count - 1; i >= 0; i--)
            {
                if (!VariantUsesAnyStripKeyword(shader, data[i], profile.stripKeywords))
                    continue;

                if (profile.neverStripBaseVariant && IsLikelyBaseVariant(data[i]))
                    continue;

                if (profile.keepAtLeastOneVariantPerSnippet && data.Count <= 1)
                    break;

                data.RemoveAt(i);
                removed++;
            }

            if (removed > 0 && profile.verboseBuildLog)
            {
                Debug.Log($"[AN Shader Variant Stripper V7] {shaderName} / {snippet.passName} / {snippet.shaderType}: stripped {removed}/{before} variants.");
            }
        }

        private static bool VariantUsesAnyStripKeyword(Shader shader, ShaderCompilerData compilerData, List<string> keywords)
        {
            for (int i = 0; i < keywords.Count; i++)
            {
                string keywordName = keywords[i];
                if (string.IsNullOrEmpty(keywordName))
                    continue;

                if (IsKeywordEnabled(shader, compilerData, keywordName))
                    return true;
            }

            return false;
        }

        private static bool IsKeywordEnabled(Shader shader, ShaderCompilerData compilerData, string keywordName)
        {
            try
            {
                ShaderKeyword localKeyword = new ShaderKeyword(shader, keywordName);
                if (compilerData.shaderKeywordSet.IsEnabled(localKeyword))
                    return true;
            }
            catch
            {
                // Some keywords are global-only or unavailable in a specific shader.
            }

            try
            {
                if (!globalKeywordCache.TryGetValue(keywordName, out ShaderKeyword globalKeyword))
                {
                    globalKeyword = new ShaderKeyword(keywordName);
                    globalKeywordCache[keywordName] = globalKeyword;
                }

                return compilerData.shaderKeywordSet.IsEnabled(globalKeyword);
            }
            catch
            {
                return false;
            }
        }

        private static bool LooksLikeURPShader(string shaderName)
        {
            if (string.IsNullOrEmpty(shaderName))
                return false;

            return shaderName.IndexOf("Universal Render Pipeline", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("Shader Graphs", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("URP", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("Universal/", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("Hidden/Universal", StringComparison.OrdinalIgnoreCase) >= 0;
        }

        private static bool IsShaderBlocked(string shaderName, ANShaderVariantAutoStripProfileV7 profile)
        {
            if (profile.shaderNameBlocklist == null)
                return false;

            for (int i = 0; i < profile.shaderNameBlocklist.Count; i++)
            {
                string block = profile.shaderNameBlocklist[i];
                if (string.IsNullOrWhiteSpace(block))
                    continue;

                if (profile.useRegexShaderNameBlocklist)
                {
                    try
                    {
                        if (Regex.IsMatch(shaderName, block, RegexOptions.IgnoreCase | RegexOptions.CultureInvariant))
                            return true;
                    }
                    catch
                    {
                        // Invalid regex patterns fall back to the legacy substring behavior below.
                    }
                }

                if (shaderName.IndexOf(block, StringComparison.OrdinalIgnoreCase) >= 0)
                    return true;
            }

            return false;
        }

        private static bool IsLikelyBaseVariant(ShaderCompilerData compilerData)
        {
            try
            {
                MethodInfo method = compilerData.shaderKeywordSet.GetType().GetMethod("GetShaderKeywords", BindingFlags.Public | BindingFlags.Instance);
                if (method == null)
                    return false;

                object value = method.Invoke(compilerData.shaderKeywordSet, null);
                if (value == null)
                    return false;

                Array array = value as Array;
                return array != null && array.Length == 0;
            }
            catch
            {
                return false;
            }
        }

        private static void WarnIfProfileHashLooksOutOfDate()
        {
            ANShaderVariantAutoStripProfileV7 profile = GetProfile();
            if (profile == null || string.IsNullOrEmpty(profile.profileVersionHash))
                return;

            string expectedHash = TryReadRecommendedKeywordHashFromLastScan();
            if (string.IsNullOrEmpty(expectedHash) || string.Equals(profile.profileVersionHash, expectedHash, StringComparison.Ordinal))
                return;

            Debug.LogWarning("[AN Shader Variant Stripper V7] The active strip profile hash differs from the last scanner recommendation hash. Re-run the scanner and regenerate the profile before shipping this build.");
        }

        private static string TryReadRecommendedKeywordHashFromLastScan()
        {
            try
            {
                if (!File.Exists(PersistedReportPath))
                    return "";

                string json = File.ReadAllText(PersistedReportPath, Encoding.UTF8);
                MatchCollection matches = Regex.Matches(json, "\"recommendedStripKeywords\"\\s*:\\s*\\[(.*?)\\]", RegexOptions.Singleline);
                if (matches.Count == 0)
                    return "";

                MatchCollection keywordMatches = Regex.Matches(matches[0].Groups[1].Value, "\"([A-Za-z0-9_]+)\"");
                List<string> keywords = new List<string>();
                foreach (Match match in keywordMatches)
                    keywords.Add(match.Groups[1].Value);

                return ComputeKeywordHash(keywords);
            }
            catch
            {
                return "";
            }
        }

        private static string ComputeKeywordHash(IEnumerable<string> keywords)
        {
            string joined = string.Join("\n", (keywords ?? Enumerable.Empty<string>()).Where(k => !string.IsNullOrWhiteSpace(k)).Select(k => k.Trim()).Distinct(StringComparer.Ordinal).OrderBy(k => k, StringComparer.Ordinal));
            using (SHA256 sha = SHA256.Create())
            {
                byte[] bytes = sha.ComputeHash(Encoding.UTF8.GetBytes(joined));
                return BitConverter.ToString(bytes).Replace("-", "").ToLowerInvariant();
            }
        }

        private static ANShaderVariantAutoStripProfileV7 GetProfile()
        {
            if (cachedProfile != null)
                return cachedProfile;

            string[] guids = AssetDatabase.FindAssets("t:" + ProfileTypeName);
            if (guids == null || guids.Length == 0)
                return null;

            cachedProfilePath = AssetDatabase.GUIDToAssetPath(guids[0]);
            cachedProfile = AssetDatabase.LoadAssetAtPath<ANShaderVariantAutoStripProfileV7>(cachedProfilePath);
            return cachedProfile;
        }
    }


