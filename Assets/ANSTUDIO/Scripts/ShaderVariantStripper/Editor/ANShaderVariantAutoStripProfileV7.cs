        private string BuildTargetTagForRule(FeatureRule rule)
        {
            string target = selectedBuildTarget.ToString();
            if (rule == null)
                return "Applies to selected build target: " + target;

            if (rule.id == "XR_STEREO")
            {
                if (IsMobileBuildTarget(selectedBuildTarget))
                    return "Selected target " + target + ": low-risk strip candidate only when XR Plug-in Management / XR runtime is disabled.";
                return "Selected target " + target + ": review if this build can be launched through OpenXR, SteamVR or another stereo runtime.";
            }

            if (rule.id == "FOVEATED_RENDERING")
                return report.detectedFeatureIds.Contains("XR_STEREO") ? "Selected target " + target + ": XR stereo detected, review foveated rendering." : "Selected target " + target + ": low risk for non-XR targets.";

            if (rule.id == "APV" || rule.id == "LIGHTMAPS")
                return "Selected target " + target + ": validate against per-quality lighting/GI setup.";

            if (rule.id == "GPU_RESIDENT_DRAWER")
                return "Selected target " + target + ": depends on URP Asset GPU Resident Drawer and Graphics BatchRendererGroup variant settings.";

            return "Applies to selected build target: " + target;
        }

        private bool IsMobileBuildTarget(BuildTarget target)
