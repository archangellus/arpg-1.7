        private void BuildVariantBudget(bool repaint)
        {
            report.shaderBudgetEntries.Clear();
            HashSet<string> shaderNamesToCheck = new HashSet<string>(report.shaderNames, StringComparer.Ordinal);

            try
            {
                foreach (string guid in AssetDatabase.FindAssets("t:Shader", SearchFolders()))
                {
                    string path = AssetDatabase.GUIDToAssetPath(guid);
                    Shader shader = AssetDatabase.LoadAssetAtPath<Shader>(path);
                    if (shader != null)
                        shaderNamesToCheck.Add(shader.name);
                }
            }
            catch
            {
                // Material-derived shader names are still usable.
            }

            int index = 0;
            foreach (string shaderName in shaderNamesToCheck.OrderBy(n => n, StringComparer.Ordinal))
            {
                if (!Progress("Building variant budget", $"{index + 1}/{shaderNamesToCheck.Count}: {shaderName}", index, shaderNamesToCheck.Count))
                    return;

                Shader shader = Shader.Find(shaderName);
                string path = shader == null ? "" : AssetDatabase.GetAssetPath(shader);
                long count = EstimateShaderVariantCount(shader);
                KeywordCountResult keywordCounts = CountShaderKeywords(shader);
                string notes = count < 0 ? "ShaderUtil variant count unavailable in this editor; count set to -1." : (count >= variantBudgetThreshold ? "Over threshold" : "Within threshold");

                if (scanKeywordSpace)
                {
                    if (keywordCounts.globalKeywordCount >= globalKeywordWarningThreshold)
                        notes += "; Global keyword count over warning threshold";
                    if (keywordCounts.localKeywordCount >= localKeywordWarningThreshold)
                        notes += "; Local keyword count over warning threshold";
                }

                report.shaderBudgetEntries.Add(new ShaderBudgetEntry
                {
                    shaderName = shaderName,
                    assetPath = path,
                    estimatedVariantCount = count,
                    threshold = variantBudgetThreshold,
                    globalKeywordCount = keywordCounts.globalKeywordCount,
                    localKeywordCount = keywordCounts.localKeywordCount,
                    srpBatcherCompatible = IsShaderSRPBatcherCompatible(shader),
                    keywordNotes = keywordCounts.notes,
                    notes = notes
                });

                index++;
            }

            report.shaderBudgetEntries = report.shaderBudgetEntries
                .OrderByDescending(e => e.estimatedVariantCount)
                .ThenBy(e => e.shaderName, StringComparer.Ordinal)
                .ToList();

            if (repaint)
            {
                SaveLastReportToDisk();
                Repaint();
            }
        }

        private long EstimateShaderVariantCount(Shader shader)
        {
            if (shader == null)
                return -1;

            Type type = typeof(ShaderUtil);
            string[] methodNames = { "GetVariantCount", "GetShaderVariantCount" };
            foreach (string methodName in methodNames)
            {
                MethodInfo[] methods = type.GetMethods(BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Static)
                    .Where(m => m.Name == methodName)
                    .ToArray();

                foreach (MethodInfo method in methods)
                {
                    try
                    {
                        ParameterInfo[] parameters = method.GetParameters();
                        object value = null;

                        if (parameters.Length == 1 && parameters[0].ParameterType == typeof(Shader))
                            value = method.Invoke(null, new object[] { shader });
                        else if (parameters.Length == 2 && parameters[0].ParameterType == typeof(Shader) && parameters[1].ParameterType == typeof(bool))
                            value = method.Invoke(null, new object[] { shader, false });
                        else
                            continue;

                        if (value == null)
                            continue;

                        return Convert.ToInt64(value);
                    }
                    catch
                    {
                        // Try the next signature.
                    }
                }
            }

            return -1;
        }


        private KeywordCountResult CountShaderKeywords(Shader shader)
        {
            KeywordCountResult result = new KeywordCountResult
            {
                globalKeywordCount = -1,
                localKeywordCount = -1,
                notes = "Keyword reflection unavailable."
            };

            if (shader == null)
                return result;

            try
            {
                object keywordSpace = typeof(Shader).GetProperty("keywordSpace", BindingFlags.Public | BindingFlags.Instance)?.GetValue(shader, null);
                if (keywordSpace != null)
                {
                    PropertyInfo keywordsProperty = keywordSpace.GetType().GetProperty("keywords", BindingFlags.Public | BindingFlags.Instance);
                    object value = keywordsProperty == null ? null : keywordsProperty.GetValue(keywordSpace, null);
                    Array array = value as Array;
                    if (array != null)
                    {
                        result.localKeywordCount = array.Length;
                        result.notes = "Read from Shader.keywordSpace.";
                    }
                }
            }
            catch
            {
                // Try ShaderUtil fallbacks below.
            }

            result.localKeywordCount = result.localKeywordCount < 0 ? TryCountShaderUtilKeywords(shader, "GetShaderLocalKeywords") : result.localKeywordCount;
            result.globalKeywordCount = TryCountShaderUtilKeywords(shader, "GetShaderGlobalKeywords");

            if (result.localKeywordCount < 0)
                result.localKeywordCount = TryCountShaderUtilKeywords(shader, "GetShaderKeywords");

            if (result.globalKeywordCount < 0 && result.localKeywordCount >= 0)
                result.globalKeywordCount = 0;

            if (result.localKeywordCount >= 0 || result.globalKeywordCount >= 0)
                result.notes = "Best-effort UnityEditor.ShaderUtil / Shader.keywordSpace reflection.";

            return result;
        }

        private int TryCountShaderUtilKeywords(Shader shader, string methodName)
        {
            try
            {
                MethodInfo[] methods = typeof(ShaderUtil).GetMethods(BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Static)
                    .Where(m => m.Name == methodName)
                    .ToArray();

                foreach (MethodInfo method in methods)
                {
                    ParameterInfo[] parameters = method.GetParameters();
                    object value = null;

                    if (parameters.Length == 1 && parameters[0].ParameterType == typeof(Shader))
                        value = method.Invoke(null, new object[] { shader });
                    else
                        continue;

                    if (value is Array)
                        return ((Array)value).Length;

                    IEnumerable enumerable = value as IEnumerable;
                    if (enumerable != null)
                    {
                        int count = 0;
                        foreach (object ignored in enumerable)
                            count++;
                        return count;
                    }
                }
            }
            catch
            {
                // Reflection fallback failed.
            }

            return -1;
        }

        private bool IsShaderSRPBatcherCompatible(Shader shader)
        {
            if (shader == null)
                return false;

            try
            {
                MethodInfo[] methods = typeof(ShaderUtil).GetMethods(BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Static)
                    .Where(m => m.Name == "IsShaderSRPBatcherCompatible")
                    .ToArray();

                foreach (MethodInfo method in methods)
                {
                    ParameterInfo[] parameters = method.GetParameters();
                    if (parameters.Length == 1 && parameters[0].ParameterType == typeof(Shader))
                    {
                        object value = method.Invoke(null, new object[] { shader });
                        if (value is bool)
                            return (bool)value;
                    }
                }
            }
            catch
            {
                // Not available in every editor.
            }

            return false;
        }

        private void BuildSRPBatcherCompatibility(bool repaint)
        {
            report.srpBatcherEntries.Clear();

            HashSet<string> shaderNamesToCheck = new HashSet<string>(report.shaderNames, StringComparer.Ordinal);
            try
            {
                foreach (string guid in AssetDatabase.FindAssets("t:Shader", SearchFolders()))
                {
                    string path = AssetDatabase.GUIDToAssetPath(guid);
                    Shader shader = AssetDatabase.LoadAssetAtPath<Shader>(path);
                    if (shader != null)
                        shaderNamesToCheck.Add(shader.name);
                }
            }
            catch
            {
                // Material-derived shader names are still usable.
            }

            int index = 0;
            foreach (string shaderName in shaderNamesToCheck.OrderBy(n => n, StringComparer.Ordinal))
            {
                if (!Progress("Building SRP Batcher table", $"{index + 1}/{shaderNamesToCheck.Count}: {shaderName}", index, shaderNamesToCheck.Count))
                    return;

                Shader shader = Shader.Find(shaderName);
                string path = shader == null ? "" : AssetDatabase.GetAssetPath(shader);
                bool compatible = IsShaderSRPBatcherCompatible(shader);

                report.srpBatcherEntries.Add(new SRPBatcherEntry
                {
                    shaderName = shaderName,
                    assetPath = path,
                    compatible = compatible,
                    notes = compatible ? "Compatible according to ShaderUtil." : InferSRPBatcherIncompatibilityReason(path)
                });

                index++;
            }

            if (repaint)
            {
                SaveLastReportToDisk();
                Repaint();
            }
        }

        private string InferSRPBatcherIncompatibilityReason(string shaderPath)
        {
            string text = TryReadProjectRelativeText(shaderPath);
            if (string.IsNullOrEmpty(text))
                return "Incompatible or unknown. Shader source was not readable.";

            List<string> reasons = new List<string>();
            if (text.IndexOf("CBUFFER_START(UnityPerMaterial)", StringComparison.Ordinal) < 0)
                reasons.Add("No standard CBUFFER_START(UnityPerMaterial) block was found.");
            if (Regex.Matches(text, @"CBUFFER_START\s*\(").Count > 1)
                reasons.Add("Multiple CBUFFER blocks found; verify per-material data is in UnityPerMaterial.");
            if (text.IndexOf("MaterialPropertyBlock", StringComparison.OrdinalIgnoreCase) >= 0)
                reasons.Add("MaterialPropertyBlock references found; these can disable SRP Batcher batching in usage.");
            if (text.IndexOf("UNITY_INSTANCING_BUFFER_START", StringComparison.Ordinal) >= 0)
                reasons.Add("Instancing buffer found; verify layout against SRP Batcher requirements.");

            return reasons.Count == 0 ? "ShaderUtil reported incompatible; inspect constant buffer layout and Shader Graph generated code." : string.Join(" ", reasons);
        }

