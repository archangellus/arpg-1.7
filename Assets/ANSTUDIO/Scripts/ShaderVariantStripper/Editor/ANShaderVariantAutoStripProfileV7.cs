        private void DrawKeywordList(List<string> keywords, bool canRemove)
        {
            if (keywords == null || keywords.Count == 0)
            {
                EditorGUILayout.LabelField("None");
                return;
            }

            List<string> removals = null;
            List<string> view = new List<string>(keywords);

            for (int i = 0; i < view.Count; i++)
            {
                string keyword = view[i];
                using (new EditorGUILayout.HorizontalScope())
                {
                    EditorGUILayout.SelectableLabel(keyword, GUILayout.Height(EditorGUIUtility.singleLineHeight));

                    if (canRemove && GUILayout.Button("Remove", GUILayout.Width(70)))
                    {
                        if (removals == null)
                            removals = new List<string>();

                        removals.Add(keyword);
                    }
                }
            }

            if (removals != null)
            {
                foreach (string keyword in removals)
                    report.recommendedStripKeywords.Remove(keyword);

                SaveLastReportToDisk();
                Repaint();
            }
        }

