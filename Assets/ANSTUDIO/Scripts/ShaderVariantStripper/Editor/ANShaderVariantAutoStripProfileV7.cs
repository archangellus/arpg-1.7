        private void SimulateStripWithCurrentProfile()
        {
            report.dryRunEntries.Clear();
            if (currentProfile == null)
                return;

            currentProfile.Sanitize();
            HashSet<string> shaders = new HashSet<string>(report.shaderNames, StringComparer.Ordinal);
            foreach (string guid in AssetDatabase.FindAssets("t:Shader", SearchFolders()))
            {
                string path = AssetDatabase.GUIDToAssetPath(guid);
                Shader shader = AssetDatabase.LoadAssetAtPath<Shader>(path);
                if (shader != null)
                    shaders.Add(shader.name);
            }

            foreach (string shaderName in shaders.OrderBy(s => s, StringComparer.Ordinal))
            {
                if (currentProfile.limitToURPShaders && !LooksLikeURPShaderName(shaderName))
                    continue;

                if (IsShaderNameBlockedByProfile(shaderName, currentProfile))
                    continue;

                Shader shader = Shader.Find(shaderName);
                string path = shader == null ? "" : AssetDatabase.GetAssetPath(shader);
                string shaderText = TryReadProjectRelativeText(path);
                List<string> matched = currentProfile.stripKeywords
                    .Where(k => !string.IsNullOrEmpty(k) && (shaderText.IndexOf(k, StringComparison.Ordinal) >= 0 || report.recommendedStripKeywords.Contains(k)))
                    .Distinct(StringComparer.Ordinal)
                    .OrderBy(k => k, StringComparer.Ordinal)
                    .ToList();

                long variants = EstimateShaderVariantCount(shader);
                long estimatedRemoved = matched.Count == 0 || variants <= 0 ? 0 : Math.Max(0, variants - (currentProfile.keepAtLeastOneVariantPerSnippet ? 1 : 0));

                report.dryRunEntries.Add(new DryRunStripEntry
                {
                    shaderName = shaderName,
                    estimatedVariantCount = variants,
                    estimatedRemovedVariants = estimatedRemoved,
                    matchedKeywords = string.Join(" ", matched)
                });
            }

            Log($"Dry-run simulation generated {report.dryRunEntries.Count} shader rows. Counts are upper-bound estimates because ShaderCompilerData is only exposed during build preprocessing.");
            SaveLastReportToDisk();
            Repaint();
        }

        private bool LooksLikeURPShaderName(string shaderName)
        {
            if (string.IsNullOrEmpty(shaderName))
                return false;

            return shaderName.IndexOf("Universal Render Pipeline", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("Shader Graphs", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("URP", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("Universal/", StringComparison.OrdinalIgnoreCase) >= 0 ||
                   shaderName.IndexOf("Hidden/Universal", StringComparison.OrdinalIgnoreCase) >= 0;
        }

        private bool IsShaderNameBlockedByProfile(string shaderName, ANShaderVariantAutoStripProfileV7 profile)
        {
            if (profile == null || profile.shaderNameBlocklist == null)
                return false;

            foreach (string block in profile.shaderNameBlocklist)
            {
                if (string.IsNullOrWhiteSpace(block))
                    continue;

                if (profile.useRegexShaderNameBlocklist)
                {
                    try
                    {
                        if (Regex.IsMatch(shaderName, block, RegexOptions.IgnoreCase | RegexOptions.CultureInvariant))
                            return true;
                    }
                    catch
                    {
                        // Fall back to substring matching.
                    }
                }

                if (shaderName.IndexOf(block, StringComparison.OrdinalIgnoreCase) >= 0)
                    return true;
            }

            return false;
        }

