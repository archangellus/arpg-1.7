        private void RunScan()
        {
            cancelRequested = false;
            report.Clear();
            report.isScanning = true;
            report.lastScanTime = DateTime.Now.ToString("yyyy-MM-dd HH:mm:ss");
            report.selectedBuildTarget = selectedBuildTarget.ToString();
            Log($"──────────────── Scan started ({selectedBuildTarget}) ────────────────");

            try
            {
                if (scanPipelineAssets)
                    ScanPipelineAssets();

                if (scanRenderPipelineGlobalSettings)
                    ScanRenderPipelineGlobalSettings();

                if (scanGraphicsApis)
                    ScanGraphicsApis();

                if (scanAlwaysIncludedShaders)
                    ScanAlwaysIncludedShaders();

                if (scanVolumeProfiles)
                    ScanVolumeProfiles();

                if (scanComputeShaders)
                    ScanComputeShaders();

                if (scanApvBakingSets)
                    ScanApvBakingSets();

                if (scanAllProjectMaterials)
                    ScanProjectMaterials();

                if (scanAllProjectPrefabs)
                    ScanPrefabs();

                if (scanAddressablesAndAssetBundles)
                    ScanAddressablesAndAssetBundles();

                if (scanShaderGraphsForKeywordReferences)
                    ScanShaderGraphsForKeywordReferences();

                if (scanShaderVariantCollections)
                    ScanShaderVariantCollections();

                if (scanVfxGraphFiles)
                    ScanVfxGraphFiles();

                if (scanRenderGraphKeywordSources)
                    ScanRenderGraphKeywordSources();

                if (scanScenes)
                    ScanScenes();

                if (scanScriptsForRuntimeKeywords)
                    ScanScriptsForRuntimeKeywords();

                BuildRecommendations();
                BuildVariantBudget(false);
                if (scanSrpbatcherCompatibility)
                    BuildSRPBatcherCompatibility(false);
                SaveLastReportToDisk();
                SaveTimestampedReportToHistory();

                Log(cancelRequested ? "Scan cancelled. Partial results kept." : "Scan finished.");
                SaveLastReportToDisk();
            }
            catch (Exception ex)
            {
                Log("Scan error: " + ex);
                Debug.LogException(ex);
            }
            finally
            {
                report.isScanning = false;
                EditorUtility.ClearProgressBar();
                Repaint();
            }
        }

        private bool Progress(string title, string info, int index, int total)
        {
            float progress = total <= 0 ? 0f : Mathf.Clamp01(index / (float)total);
            return ProgressValue(title, info, progress);
        }

        private bool ProgressValue(string title, string info, float progress)
        {
            if (cancelRequested)
                return false;

            if (Application.isBatchMode)
                return true;

            if (EditorUtility.DisplayCancelableProgressBar(title, info, Mathf.Clamp01(progress)))
            {
                cancelRequested = true;
                return false;
            }

            return true;
        }

        private void ScanProjectMaterials()
        {
            string[] guids = AssetDatabase.FindAssets("t:Material", SearchFolders());
            for (int i = 0; i < guids.Length; i++)
            {
                if (!Progress("Scanning materials", $"{i + 1}/{guids.Length}", i, guids.Length))
                    return;

                string path = AssetDatabase.GUIDToAssetPath(guids[i]);
                if (!ShouldScanAssetPath(path))
                    continue;

                Material material = AssetDatabase.LoadAssetAtPath<Material>(path);
                ScanMaterial(material, path);
            }
        }

        private void ScanPrefabs()
        {
            string[] guids = AssetDatabase.FindAssets("t:Prefab", SearchFolders());
            for (int i = 0; i < guids.Length; i++)
            {
                if (!Progress("Scanning prefabs", $"{i + 1}/{guids.Length}", i, guids.Length))
                    return;

                string path = AssetDatabase.GUIDToAssetPath(guids[i]);
                if (!ShouldScanAssetPath(path))
                    continue;

                GameObject prefab = AssetDatabase.LoadAssetAtPath<GameObject>(path);
                if (prefab == null)
                    continue;

                Renderer[] renderers = prefab.GetComponentsInChildren<Renderer>(true);
                foreach (Renderer renderer in renderers)
                    ScanRenderer(renderer, path);

                LODGroup[] lodGroups = prefab.GetComponentsInChildren<LODGroup>(true);
                if (lodGroups.Length > 0)
                    MarkFeature("LOD_CROSSFADE", "Prefab contains LODGroup: " + path);

                ScanComponents(prefab.GetComponentsInChildren<Component>(true), path);
            }
        }




        private void ScanUrpBuiltInStripSettings(RenderPipelineAsset asset, string path)
        {
            if (asset == null)
                return;

            string typeName = asset.GetType().Name;
            string fullName = asset.GetType().FullName ?? typeName;
            if (typeName.IndexOf("UniversalRenderPipelineAsset", StringComparison.OrdinalIgnoreCase) < 0 &&
                fullName.IndexOf("UniversalRenderPipelineAsset", StringComparison.OrdinalIgnoreCase) < 0)
            {
                return;
            }

            bool foundAny = false;
            UrpStripSettingsEntry entry = new UrpStripSettingsEntry
            {
                assetPath = path,
                shaderVariantLogLevel = ReadSerializedPropertyAsString(asset, "m_ShaderVariantLogLevel", ref foundAny),
                stripDebugVariants = ReadSerializedBool(asset, "m_StripDebugVariants", false, ref foundAny),
                stripUnusedPostProcessingVariants = ReadSerializedBool(asset, "m_StripUnusedPostProcessingVariants", false, ref foundAny),
                stripUnusedVariants = ReadSerializedBool(asset, "m_StripUnusedVariants", false, ref foundAny)
            };

            if (foundAny)
                report.urpStripSettingsEntries.Add(entry);
        }

        private bool ReadSerializedBool(UnityEngine.Object obj, string propertyName, bool defaultValue, ref bool foundAny)
        {
            try
            {
                SerializedObject serializedObject = new SerializedObject(obj);
                SerializedProperty property = serializedObject.FindProperty(propertyName);
                if (property != null)
                {
                    foundAny = true;
                    if (property.propertyType == SerializedPropertyType.Boolean)
                        return property.boolValue;

                    if (property.propertyType == SerializedPropertyType.Integer)
                        return property.intValue != 0;
                }
            }
            catch
            {
                // Advisory only.
            }

            return defaultValue;
        }

        private string ReadSerializedPropertyAsString(UnityEngine.Object obj, string propertyName, ref bool foundAny)
        {
            try
            {
                SerializedObject serializedObject = new SerializedObject(obj);
                SerializedProperty property = serializedObject.FindProperty(propertyName);
                if (property != null)
                {
                    foundAny = true;
                    return SerializedPropertyValueToString(property);
                }
            }
            catch
            {
                // Advisory only.
            }

            return "";
        }

        private void ScanGraphicsApis()
        {
            report.graphicsApiNames.Clear();

            try
            {
                GraphicsDeviceType[] apis = PlayerSettings.GetGraphicsAPIs(selectedBuildTarget);
                if (apis != null && apis.Length > 0)
                {
                    foreach (GraphicsDeviceType api in apis)
                        report.graphicsApiNames.Add(api.ToString());
                }

                bool automatic = false;
                try
                {
                    automatic = PlayerSettings.GetUseDefaultGraphicsAPIs(selectedBuildTarget);
                }
                catch
                {
                    // Some build targets do not expose the automatic API switch.
                }

                if (automatic)
                    report.graphicsApiNames.Add("Automatic graphics API selection");

                if (report.graphicsApiNames.Count == 0)
                    report.graphicsApiNames.Add("Unavailable for " + selectedBuildTarget);

                foreach (string note in BuildGraphicsApiCompatibilityNotes())
                    report.graphicsApiNotes.Add(note);
            }
            catch (Exception ex)
            {
                report.graphicsApiNames.Add("Unavailable: " + ex.Message);
                Log("Graphics API scan skipped: " + ex.Message);
            }
        }

        private List<string> BuildGraphicsApiCompatibilityNotes()
        {
            List<string> notes = new List<string>();
            HashSet<string> apis = new HashSet<string>(report.graphicsApiNames, StringComparer.OrdinalIgnoreCase);

            if (apis.Any(a => a.IndexOf("OpenGLES2", StringComparison.OrdinalIgnoreCase) >= 0))
                notes.Add("OpenGL ES 2.0 was detected. Avoid relying on deferred-only or compute-heavy URP paths for this build target.");

            if (apis.Any(a => a.IndexOf("OpenGLES3", StringComparison.OrdinalIgnoreCase) >= 0))
                notes.Add("OpenGL ES 3.x was detected. Review compute, XR multiview and advanced renderer-feature recommendations for mobile fallback compatibility.");

            if (apis.Any(a => a.IndexOf("Vulkan", StringComparison.OrdinalIgnoreCase) >= 0))
                notes.Add("Vulkan was detected. XR multiview and compute-driven renderer features are generally more viable than on OpenGL ES fallback paths.");

            if (apis.Any(a => a.IndexOf("Metal", StringComparison.OrdinalIgnoreCase) >= 0))
                notes.Add("Metal was detected. Keep compute-backed URP renderer feature variants only when those features are actually present on iOS/macOS quality tiers.");

            if (apis.Any(a => a.IndexOf("Direct3D", StringComparison.OrdinalIgnoreCase) >= 0 || a.IndexOf("D3D", StringComparison.OrdinalIgnoreCase) >= 0))
                notes.Add("Direct3D was detected. Desktop deferred/Forward+ variants may be valid; compare with mobile API lists before sharing one strip profile across targets.");

            return notes.Distinct().ToList();
        }

        private void ScanAlwaysIncludedShaders()
        {
            HashSet<string> shaderPaths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

            UnityEngine.Object graphicsSettings = TryGetGraphicsSettingsObject();
            if (graphicsSettings != null)
            {
                try
                {
                    SerializedObject serializedObject = new SerializedObject(graphicsSettings);
                    SerializedProperty property = serializedObject.GetIterator();
                    int guard = 0;
                    while (property.NextVisible(true))
                    {
                        if (++guard > 10000)
                            break;

                        if (property.propertyType == SerializedPropertyType.ObjectReference && property.objectReferenceValue is Shader)
                        {
                            string path = AssetDatabase.GetAssetPath(property.objectReferenceValue);
                            if (!string.IsNullOrEmpty(path))
                                shaderPaths.Add(path);
                        }
                    }
                }
                catch
                {
                    // Fall back to YAML parsing below.
                }
            }

            string settingsPath = Path.Combine(Directory.GetCurrentDirectory(), "ProjectSettings/GraphicsSettings.asset");
            if (File.Exists(settingsPath))
            {
                try
                {
                    string yaml = File.ReadAllText(settingsPath, Encoding.UTF8);
                    MatchCollection matches = Regex.Matches(yaml, @"m_AlwaysIncludedShaders:[\s\S]*?(?=\n\s*m_|$)");
                    foreach (Match section in matches)
                    {
                        foreach (Match guidMatch in Regex.Matches(section.Value, @"guid:\s*([0-9a-fA-F]{32})"))
                        {
                            string assetPath = AssetDatabase.GUIDToAssetPath(guidMatch.Groups[1].Value);
                            if (!string.IsNullOrEmpty(assetPath))
                                shaderPaths.Add(assetPath);
                        }
                    }
                }
                catch
                {
                    // Advisory only.
                }
            }

            int index = 0;
            foreach (string shaderPath in shaderPaths.OrderBy(p => p, StringComparer.OrdinalIgnoreCase))
            {
                if (!Progress("Auditing Always Included Shaders", $"{index + 1}/{shaderPaths.Count}: {shaderPath}", index, shaderPaths.Count))
                    return;

                Shader shader = AssetDatabase.LoadAssetAtPath<Shader>(shaderPath);
                if (shader == null)
                {
                    index++;
                    continue;
                }

                long variants = EstimateShaderVariantCount(shader);
                string note = LooksLikeBroadAlwaysIncludedShader(shader.name)
                    ? "Broad/catch-all shader name; consider removing from Always Included Shaders and using an SVC or direct references instead."
                    : "Always included; Unity includes variants for this shader even when scene/material evidence is sparse.";

                report.alwaysIncludedShaderEntries.Add(new AlwaysIncludedShaderEntry
                {
                    shaderName = shader.name,
                    assetPath = shaderPath,
                    estimatedVariantCount = variants,
                    notes = note
                });

                report.shaderNames.Add(shader.name);
                index++;
            }

            if (report.alwaysIncludedShaderEntries.Count > 0)
                Log($"Always Included Shaders auditor found {report.alwaysIncludedShaderEntries.Count} entries.");
        }

        private UnityEngine.Object TryGetGraphicsSettingsObject()
        {
            try
            {
                MethodInfo method = typeof(GraphicsSettings).GetMethod("GetGraphicsSettings", BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Static);
                if (method != null)
                    return method.Invoke(null, null) as UnityEngine.Object;
            }
            catch
            {
                // Fall back to serialized ProjectSettings parsing.
            }

            return null;
        }

        private bool LooksLikeBroadAlwaysIncludedShader(string shaderName)
        {
            if (string.IsNullOrEmpty(shaderName))
                return false;

            string n = shaderName.ToLowerInvariant();
            return n == "standard" ||
                   n.Contains("/lit") ||
                   n.Contains("universal render pipeline/lit") ||
                   n.Contains("universal render pipeline/complex lit") ||
                   n.Contains("shader graphs/") ||
                   n.Contains("terrain/lit");
        }

        private void ScanVolumeProfiles()
        {
            HashSet<string> paths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

            try
            {
                foreach (string guid in AssetDatabase.FindAssets("t:VolumeProfile", SearchFolders()))
                {
                    string path = AssetDatabase.GUIDToAssetPath(guid);
                    if (!string.IsNullOrEmpty(path) && ShouldScanAssetPath(path))
                        paths.Add(path);
                }
            }
            catch
            {
                // Some package/editor combinations do not expose this type filter. Extension fallback below handles assets.
            }

            foreach (string path in AssetDatabase.GetAllAssetPaths())
            {
                if (ShouldScanAssetPath(path) && path.EndsWith(".asset", StringComparison.OrdinalIgnoreCase))
                {
                    ScriptableObject so = AssetDatabase.LoadAssetAtPath<ScriptableObject>(path);
                    if (so != null && so.GetType().Name.IndexOf("VolumeProfile", StringComparison.OrdinalIgnoreCase) >= 0)
                        paths.Add(path);
                }
            }

            int index = 0;
            foreach (string path in paths.OrderBy(p => p, StringComparer.OrdinalIgnoreCase))
            {
                if (!Progress("Scanning Volume Profiles", $"{index + 1}/{paths.Count}: {path}", index, paths.Count))
                    return;

                ScriptableObject profile = AssetDatabase.LoadAssetAtPath<ScriptableObject>(path);
                if (profile != null)
                    ScanVolumeProfileAsset(profile, path);

                index++;
            }

            if (paths.Count > 0)
                Log($"Volume Profile scanner found {report.volumeProfileEntries.Count} active component entries.");
        }

        private void ScanVolumeProfileAsset(UnityEngine.Object profile, string path)
        {
            if (profile == null)
                return;

            IEnumerable<UnityEngine.Object> components = ReadVolumeProfileComponents(profile);
            bool foundComponent = false;

            foreach (UnityEngine.Object component in components)
            {
                if (component == null)
                    continue;

                foundComponent = true;
                ScanVolumeComponent(component, path);
            }

            if (!foundComponent)
                ScanSerializedObjectForFeatureEvidence(profile, path);
        }

        private IEnumerable<UnityEngine.Object> ReadVolumeProfileComponents(UnityEngine.Object profile)
        {
            List<UnityEngine.Object> result = new List<UnityEngine.Object>();

            try
            {
                FieldInfo field = profile.GetType().GetField("components", BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Instance);
                object fieldValue = field == null ? null : field.GetValue(profile);
                IEnumerable enumerable = fieldValue as IEnumerable;
                if (enumerable != null)
                {
                    foreach (object item in enumerable)
                    {
                        UnityEngine.Object obj = item as UnityEngine.Object;
                        if (obj != null)
                            result.Add(obj);
                    }
                }
            }
            catch
            {
                // Fall through to serialized scan.
            }

            if (result.Count > 0)
                return result;

            try
            {
                SerializedObject serializedObject = new SerializedObject(profile);
                SerializedProperty iterator = serializedObject.GetIterator();
                int guard = 0;
                while (iterator.NextVisible(true))
                {
                    if (++guard > 10000)
                        break;

                    if (iterator.propertyType == SerializedPropertyType.ObjectReference && iterator.objectReferenceValue != null)
                    {
                        string typeName = iterator.objectReferenceValue.GetType().Name;
                        if (typeName.IndexOf("Volume", StringComparison.OrdinalIgnoreCase) >= 0 ||
                            typeName.IndexOf("Bloom", StringComparison.OrdinalIgnoreCase) >= 0 ||
                            typeName.IndexOf("MotionBlur", StringComparison.OrdinalIgnoreCase) >= 0 ||
                            typeName.IndexOf("Tonemapping", StringComparison.OrdinalIgnoreCase) >= 0 ||
                            typeName.IndexOf("DepthOfField", StringComparison.OrdinalIgnoreCase) >= 0)
                        {
                            result.Add(iterator.objectReferenceValue);
                        }
                    }
                }
            }
            catch
            {
                // Ignore.
            }

            return result;
        }

        private void ScanVolumeComponent(UnityEngine.Object component, string profilePath)
        {
            if (component == null)
                return;

            string typeName = component.GetType().Name;
            string lower = typeName.ToLowerInvariant();

            bool active = ReadBoolMemberOrSerialized(component, "active", true);
            List<string> inferredKeywords = new List<string>();

            if (!active)
            {
                report.volumeProfileEntries.Add(new VolumeProfileEntry
                {
                    profilePath = profilePath,
                    componentName = typeName,
                    active = false,
                    inferredKeywords = ""
                });
                return;
            }

            if (lower.Contains("motionblur"))
            {
                MarkFeature("MOTION_VECTORS", "Volume Profile uses Motion Blur: " + profilePath);
                inferredKeywords.Add("MOTION_VECTORS_ON");
            }

            if (lower.Contains("temporal") || lower == "taa" || lower.Contains("temporalaa"))
            {
                MarkFeature("POST_PROCESS_AA", "Volume Profile uses temporal anti-aliasing: " + profilePath);
                MarkFeature("TEMPORAL_UPSCALING", "Volume Profile uses temporal anti-aliasing / upsampling: " + profilePath);
                MarkFeature("MOTION_VECTORS", "Temporal AA requires motion vectors: " + profilePath);
                inferredKeywords.Add("_TAA");
                inferredKeywords.Add("_TAA_UPSAMPLING");
                inferredKeywords.Add("MOTION_VECTORS_ON");
            }

            if (lower.Contains("filmgrain") || lower.Contains("bloom") || lower.Contains("chromatic") ||
                lower.Contains("depthoffield") || lower.Contains("lensdistortion") || lower.Contains("vignette") ||
                lower.Contains("tonemapping") || lower.Contains("coloradjustments") || lower.Contains("whitebalance"))
            {
                report.detectedFeatures.Add("URP post-processing volume component: " + typeName + " | " + profilePath);
            }

            if (lower.Contains("screenspacelensflare") || lower.Contains("lensflare"))
            {
                MarkFeature("SCREEN_SPACE_LENS_FLARE", "Volume Profile uses Screen Space Lens Flare: " + profilePath);
                inferredKeywords.Add("_SCREEN_SPACE_LENS_FLARE");
            }

            if (inferredKeywords.Count > 0 || active)
            {
                report.volumeProfileEntries.Add(new VolumeProfileEntry
                {
                    profilePath = profilePath,
                    componentName = typeName,
                    active = active,
                    inferredKeywords = string.Join(" ", inferredKeywords.Distinct().OrderBy(k => k, StringComparer.Ordinal))
                });
            }

            ScanSerializedObjectForFeatureEvidence(component, profilePath);
        }

        private bool ReadBoolMemberOrSerialized(UnityEngine.Object obj, string memberName, bool defaultValue)
        {
            if (obj == null)
                return defaultValue;

            try
            {
                FieldInfo field = obj.GetType().GetField(memberName, BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Instance);
                if (field != null && field.FieldType == typeof(bool))
                    return (bool)field.GetValue(obj);

                PropertyInfo property = obj.GetType().GetProperty(memberName, BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Instance);
                if (property != null && property.PropertyType == typeof(bool))
                    return (bool)property.GetValue(obj, null);

                SerializedObject serializedObject = new SerializedObject(obj);
                SerializedProperty propertySerialized = serializedObject.FindProperty(memberName);
                if (propertySerialized != null && propertySerialized.propertyType == SerializedPropertyType.Boolean)
                    return propertySerialized.boolValue;
            }
            catch
            {
                // Advisory only.
            }

            return defaultValue;
        }

        private void ScanComputeShaders()
        {
            HashSet<string> paths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

            try
            {
                foreach (string guid in AssetDatabase.FindAssets("t:ComputeShader", SearchFolders()))
                {
                    string path = AssetDatabase.GUIDToAssetPath(guid);
                    if (!string.IsNullOrEmpty(path) && ShouldScanAssetPath(path))
                        paths.Add(path);
                }
            }
            catch
            {
                // Extension fallback below.
            }

            foreach (string path in AssetDatabase.GetAllAssetPaths())
            {
                if (ShouldScanAssetPath(path) && path.EndsWith(".compute", StringComparison.OrdinalIgnoreCase))
                    paths.Add(path);
            }

            int index = 0;
            foreach (string path in paths.OrderBy(p => p, StringComparer.OrdinalIgnoreCase))
            {
                if (!Progress("Scanning Compute Shaders", $"{index + 1}/{paths.Count}: {path}", index, paths.Count))
                    return;

                ScanComputeShaderText(path);
                index++;
            }

            if (paths.Count > 0)
                Log($"Compute shader scan found {report.computeShaderKeywordReferences.Count} keyword references in {report.computeShaderSourcePaths.Count} files.");
        }

        private void ScanComputeShaderText(string path)
        {
            string text = TryReadProjectRelativeText(path);
            if (string.IsNullOrEmpty(text))
                return;

            int before = report.computeShaderKeywordReferences.Count;
            string[] lines = text.Split(new[] { '\n', '\r' }, StringSplitOptions.RemoveEmptyEntries);

            foreach (string rawLine in lines)
            {
                string line = StripLineComment(rawLine).Trim();
                if (line.IndexOf("#pragma", StringComparison.OrdinalIgnoreCase) < 0)
                    continue;

                Match variantPragma = Regex.Match(line, @"#pragma\s+(?:multi_compile|multi_compile_local|shader_feature|shader_feature_local)\s+(.+)$", RegexOptions.IgnoreCase);
                if (variantPragma.Success)
                {
                    RegisterComputePragmaTokens(variantPragma.Groups[1].Value, path);
                    continue;
                }

                Match kernelPragma = Regex.Match(line, @"#pragma\s+kernel\s+\S+\s*(.*)$", RegexOptions.IgnoreCase);
                if (kernelPragma.Success)
                    RegisterComputePragmaTokens(kernelPragma.Groups[1].Value, path);
            }

            if (report.computeShaderKeywordReferences.Count > before)
            {
                report.computeShaderSourcePaths.Add(path);
                report.detectedFeatures.Add("Compute Shader keyword variants: " + path);
            }
        }

        private string StripLineComment(string line)
        {
            if (line == null)
                return "";

            int index = line.IndexOf("//", StringComparison.Ordinal);
            return index >= 0 ? line.Substring(0, index) : line;
        }

        private void RegisterComputePragmaTokens(string tokenText, string path)
        {
            if (string.IsNullOrEmpty(tokenText))
                return;

            foreach (string token in Regex.Split(tokenText, @"\s+"))
            {
                string keyword = token.Trim().Trim('[', ']', '"', '\'');
                if (keyword == "_" || string.IsNullOrEmpty(keyword))
                    continue;

                RegisterEvidenceKeyword(keyword, report.computeShaderKeywordReferences, "Compute shader pragma: " + path);
            }
        }

        private void ScanApvBakingSets()
        {
            HashSet<string> paths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

            string[] filters = { "t:ProbeVolumeBakingSet", "t:ProbeVolumePerSceneData" };
            foreach (string filter in filters)
            {
                try
                {
                    foreach (string guid in AssetDatabase.FindAssets(filter, SearchFolders()))
                    {
                        string path = AssetDatabase.GUIDToAssetPath(guid);
                        if (!string.IsNullOrEmpty(path) && ShouldScanAssetPath(path))
                            paths.Add(path);
                    }
                }
                catch
                {
                    // Type filters are version-dependent.
                }
            }

            foreach (string guid in AssetDatabase.FindAssets("t:ScriptableObject", SearchFolders()))
            {
                string path = AssetDatabase.GUIDToAssetPath(guid);
                if (!ShouldScanAssetPath(path))
                    continue;

                ScriptableObject so = AssetDatabase.LoadAssetAtPath<ScriptableObject>(path);
                if (so == null)
                    continue;

                string typeName = so.GetType().Name;
                if (typeName.IndexOf("ProbeVolumeBakingSet", StringComparison.OrdinalIgnoreCase) >= 0 ||
                    typeName.IndexOf("ProbeVolumePerSceneData", StringComparison.OrdinalIgnoreCase) >= 0)
                {
                    paths.Add(path);
                }
            }

            int index = 0;
            foreach (string path in paths.OrderBy(p => p, StringComparer.OrdinalIgnoreCase))
            {
                if (!Progress("Scanning Adaptive Probe Volume assets", $"{index + 1}/{paths.Count}: {path}", index, paths.Count))
                    return;

                ScriptableObject so = AssetDatabase.LoadAssetAtPath<ScriptableObject>(path);
                if (so != null)
                    ScanApvAsset(so, path);

                index++;
            }
        }

        private void ScanApvAsset(UnityEngine.Object obj, string path)
        {
            if (obj == null)
                return;

            MarkFeature("APV", "Adaptive Probe Volume asset detected: " + obj.GetType().Name + " | " + path);

            List<string> hints = new List<string>();
            try
            {
                SerializedObject serializedObject = new SerializedObject(obj);
                SerializedProperty iterator = serializedObject.GetIterator();
                int guard = 0;

                while (iterator.NextVisible(true))
                {
                    if (++guard > 10000)
                        break;

                    string name = ((iterator.name ?? "") + " " + (iterator.displayName ?? "")).ToLowerInvariant();
                    if (name.Contains("stream") || name.Contains("memorybudget") || name.Contains("memory budget") || name.Contains("probe volume"))
                    {
                        string value = SerializedPropertyValueToString(iterator);
                        if (!string.IsNullOrEmpty(value))
                            hints.Add(iterator.name + "=" + value);
                    }
                }
            }
            catch
            {
                // Advisory only.
            }

            report.apvStreamingEntries.Add(new ApvStreamingEntry
            {
                assetPath = path,
                assetType = obj.GetType().Name,
                notes = hints.Count == 0 ? "APV asset detected; serialized streaming budget fields were not found by the scanner." : string.Join("; ", hints.Take(12))
            });
        }

        private string SerializedPropertyValueToString(SerializedProperty property)
        {
            if (property == null)
                return "";

            switch (property.propertyType)
            {
                case SerializedPropertyType.Boolean: return property.boolValue.ToString();
                case SerializedPropertyType.Integer: return property.intValue.ToString();
                case SerializedPropertyType.Float: return property.floatValue.ToString();
                case SerializedPropertyType.String: return property.stringValue;
                case SerializedPropertyType.Enum: return property.enumDisplayNames != null && property.enumValueIndex >= 0 && property.enumValueIndex < property.enumDisplayNames.Length ? property.enumDisplayNames[property.enumValueIndex] : property.enumValueIndex.ToString();
                case SerializedPropertyType.ObjectReference: return property.objectReferenceValue == null ? "" : property.objectReferenceValue.name;
                default: return "";
            }
        }


        private void ScanRendererDataInventory(ScriptableObject rendererData, string path)
        {
            if (rendererData == null)
                return;

            if (report.rendererDataEntries.Any(e => string.Equals(e.assetPath, path, StringComparison.OrdinalIgnoreCase)))
                return;

            RendererDataEntry entry = new RendererDataEntry
            {
                assetPath = path,
                typeName = rendererData.GetType().Name,
                renderingPath = ReadRenderingPathFromSerializedObject(rendererData)
            };

            HashSet<string> features = new HashSet<string>(StringComparer.Ordinal);
            HashSet<string> keywords = new HashSet<string>(StringComparer.Ordinal);

            try
            {
                SerializedObject serializedObject = new SerializedObject(rendererData);
                SerializedProperty iterator = serializedObject.GetIterator();
                int guard = 0;

                while (iterator.NextVisible(true))
                {
                    if (++guard > 20000)
                        break;

                    if (iterator.propertyType != SerializedPropertyType.ObjectReference || iterator.objectReferenceValue == null)
                        continue;

                    UnityEngine.Object reference = iterator.objectReferenceValue;
                    string typeName = reference.GetType().Name;
                    string featurePath = AssetDatabase.GetAssetPath(reference);

                    if (typeName.IndexOf("RendererFeature", StringComparison.OrdinalIgnoreCase) >= 0 ||
                        typeName.IndexOf("ScriptableRendererFeature", StringComparison.OrdinalIgnoreCase) >= 0 ||
                        featurePath.IndexOf("RendererFeature", StringComparison.OrdinalIgnoreCase) >= 0)
                    {
                        string featureLabel = typeName + " | " + (string.IsNullOrEmpty(featurePath) ? reference.name : featurePath);
                        features.Add(featureLabel);
                        report.rendererFeatures.Add(featureLabel);

                        foreach (string keyword in InferRendererFeatureKeywords(reference, featurePath))
                            keywords.Add(keyword);

                        DetectFeatureFromTypeName(typeName, string.IsNullOrEmpty(featurePath) ? path : featurePath);
                    }
                }
            }
            catch
            {
                // Renderer data serialization can vary between URP versions. Keep partial entry.
            }

            string rendererText = TryReadProjectRelativeText(path);
            if (!string.IsNullOrEmpty(rendererText))
            {
                foreach (Match match in Regex.Matches(rendererText, @"\b_?[A-Z][A-Z0-9_]{2,}\b"))
                {
                    string keyword = match.Value;
                    if (LooksLikeShaderKeywordToken(keyword))
                        keywords.Add(keyword);
                }
            }

            entry.rendererFeatures = features.Count == 0 ? "None detected" : string.Join("; ", features.OrderBy(f => f, StringComparer.Ordinal));
            entry.injectedKeywords = keywords.Count == 0 ? "None inferred" : string.Join(" ", keywords.OrderBy(k => k, StringComparer.Ordinal));
            report.rendererDataEntries.Add(entry);

            if (entry.renderingPath.IndexOf("Forward+", StringComparison.OrdinalIgnoreCase) >= 0)
                report.detectedFeatures.Add("URP renderer uses Forward+: " + path);
            if (entry.renderingPath.IndexOf("Deferred", StringComparison.OrdinalIgnoreCase) >= 0)
                report.detectedFeatures.Add("URP renderer uses Deferred: " + path);
        }

        private string ReadRenderingPathFromSerializedObject(UnityEngine.Object obj)
        {
            int bestValue = int.MinValue;
            string bestName = "";

            try
            {
                SerializedObject serializedObject = new SerializedObject(obj);
                SerializedProperty iterator = serializedObject.GetIterator();
                int guard = 0;

                while (iterator.NextVisible(true))
                {
                    if (++guard > 20000)
                        break;

                    string name = (iterator.name ?? "").ToLowerInvariant();
                    string display = (iterator.displayName ?? "").ToLowerInvariant();
                    string combined = name + " " + display;

                    if (iterator.propertyType == SerializedPropertyType.Enum &&
                        (combined.Contains("renderingpath") || combined.Contains("rendering path") || combined.Contains("renderingmode") || combined.Contains("rendering mode")))
                    {
                        if (iterator.enumDisplayNames != null && iterator.enumValueIndex >= 0 && iterator.enumValueIndex < iterator.enumDisplayNames.Length)
                            return iterator.enumDisplayNames[iterator.enumValueIndex] + " (" + iterator.enumValueIndex + ")";
                    }

                    if (iterator.propertyType == SerializedPropertyType.Integer &&
                        (combined.Contains("renderingpath") || combined.Contains("rendering path") || combined.Contains("renderingmode") || combined.Contains("rendering mode")))
                    {
                        bestValue = iterator.intValue;
                        bestName = iterator.name;
                    }

                    if (iterator.propertyType == SerializedPropertyType.String && !string.IsNullOrEmpty(iterator.stringValue))
                    {
                        string value = iterator.stringValue;
                        if (value.IndexOf("Forward+", StringComparison.OrdinalIgnoreCase) >= 0 ||
                            value.IndexOf("ForwardPlus", StringComparison.OrdinalIgnoreCase) >= 0 ||
                            value.IndexOf("Deferred", StringComparison.OrdinalIgnoreCase) >= 0)
                        {
                            return value;
                        }
                    }
                }
            }
            catch
            {
                // Advisory only.
            }

            if (bestValue != int.MinValue)
                return MapRendererPathInt(bestName, bestValue);

            return "Unknown";
        }

        private string MapRendererPathInt(string propertyName, int value)
        {
            string lower = (propertyName ?? "").ToLowerInvariant();
            string mapped = "Unknown";

            // Unity 6 URP serializes the renderer path as an integer. The enum names have changed across URP versions,
            // so this mapping is intentionally descriptive rather than authoritative.
            if (value == 0)
                mapped = "Forward";
            else if (value == 1)
                mapped = "Deferred";
            else if (value == 2)
                mapped = "Forward+";
            else if (value == 3)
                mapped = "Deferred+";

            if (lower.Contains("renderingmode") && value == 1)
                mapped = "Deferred";

            return mapped + " (" + propertyName + "=" + value + ")";
        }

        private IEnumerable<string> InferRendererFeatureKeywords(UnityEngine.Object rendererFeature, string featurePath)
        {
            HashSet<string> keywords = new HashSet<string>(StringComparer.Ordinal);

            if (rendererFeature == null)
                return keywords;

            string typeName = rendererFeature.GetType().Name;
            string combined = (typeName + " " + rendererFeature.name + " " + featurePath).ToLowerInvariant();

            if (combined.Contains("ambientocclusion") || combined.Contains("ssao"))
                keywords.Add("_SCREEN_SPACE_OCCLUSION");

            if (combined.Contains("decal"))
            {
                keywords.Add("_DBUFFER_MRT1");
                keywords.Add("_DBUFFER_MRT2");
                keywords.Add("_DBUFFER_MRT3");
            }

            if (combined.Contains("lensflare"))
                keywords.Add("_SCREEN_SPACE_LENS_FLARE");

            if (combined.Contains("renderpass") || combined.Contains("native"))
                keywords.Add("_RENDER_PASS_ENABLED");

            if (combined.Contains("water"))
            {
                keywords.Add("_WATER_WAVES");
                keywords.Add("_UNDER_WATER");
            }

            if (combined.Contains("screen") && combined.Contains("shadow"))
                keywords.Add("_MAIN_LIGHT_SHADOWS_SCREEN");

            string text = TryReadProjectRelativeText(featurePath);
            if (!string.IsNullOrEmpty(text))
            {
                foreach (Match match in Regex.Matches(text, @"\b_?[A-Z][A-Z0-9_]{2,}\b"))
                {
                    string keyword = match.Value;
                    if (LooksLikeShaderKeywordToken(keyword))
                        keywords.Add(keyword);
                }
            }

            foreach (string keyword in keywords.ToList())
                DetectFeatureFromKeyword(keyword, "Renderer feature keyword inference: " + featurePath);

            return keywords;
        }

