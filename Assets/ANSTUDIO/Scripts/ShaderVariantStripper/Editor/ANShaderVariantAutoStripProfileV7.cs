                scanned++;
            }

            if (scanned > 0)
                Log($"AssetBundle scan found {scanned} marked assets and {report.assetBundleKeywords.Count} keywords.");
        }

        private IEnumerable<string> CollectGuidStringsFromAsset(UnityEngine.Object asset, string path)
        {
            HashSet<string> result = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

            if (asset != null)
            {
                try
                {
                    SerializedObject serializedObject = new SerializedObject(asset);
                    SerializedProperty iterator = serializedObject.GetIterator();
                    int guard = 0;

                    while (iterator.NextVisible(true))
                    {
                        guard++;
                        if (guard > 100000)
                            break;

                        if (iterator.propertyType != SerializedPropertyType.String)
                            continue;

                        string value = iterator.stringValue;
                        if (LooksLikeGuid(value))
                            result.Add(value);
                    }
                }
                catch
                {
                    // Fall back to text parsing below.
                }
            }

            string fullPath = Path.Combine(Directory.GetCurrentDirectory(), path);
            if (File.Exists(fullPath))
            {
                try
                {
                    string text = File.ReadAllText(fullPath);
                    foreach (Match match in Regex.Matches(text, @"\b[0-9a-fA-F]{32}\b"))
                        result.Add(match.Value);
                }
                catch
                {
                    // Ignore unreadable package or generated files.
                }
            }

            return result;
        }

        private bool LooksLikeGuid(string value)
        {
            return !string.IsNullOrEmpty(value) && Regex.IsMatch(value, "^[0-9a-fA-F]{32}$");
        }

        private void ScanExternalAssetDependencies(string assetPath, HashSet<string> materialPathSet, HashSet<string> keywordSet, string sourceLabel)
        {
            if (string.IsNullOrEmpty(assetPath))
                return;

            string[] dependencies;
            try
            {
                dependencies = AssetDatabase.GetDependencies(assetPath, true);
            }
            catch
            {
                dependencies = new[] { assetPath };
            }

            foreach (string dep in dependencies)
            {
                if (!ShouldScanAssetPath(dep))
                    continue;

                Material material = AssetDatabase.LoadAssetAtPath<Material>(dep);
                if (material == null)
                    continue;

                materialPathSet.Add(dep);
                ScanMaterial(material, dep);
                AddMaterialKeywordsToSet(material, keywordSet, sourceLabel + " material keyword: " + dep);
            }
        }

        private void AddMaterialKeywordsToSet(Material material, HashSet<string> target, string reason)
        {
            if (material == null || target == null)
                return;

            foreach (string keyword in ReadMaterialKeywordNames(material))
            {
                target.Add(keyword);
                DetectFeatureFromKeyword(keyword, reason);
            }
        }

        private IEnumerable<string> ReadMaterialKeywordNames(Material material)
        {
            HashSet<string> result = new HashSet<string>(StringComparer.Ordinal);

            if (material == null)
                return result;

            try
            {
                LocalKeyword[] enabled = material.enabledKeywords;
                if (enabled != null)
                {
                    foreach (LocalKeyword keyword in enabled)
                    {
                        if (!string.IsNullOrEmpty(keyword.name))
                            result.Add(keyword.name);
                    }
                }
            }
            catch
            {
                // Fallback below.
            }

#pragma warning disable 0618
            try
            {
                string[] keywords = material.shaderKeywords;
                if (keywords != null)
                {
                    foreach (string keyword in keywords)
                    {
                        if (!string.IsNullOrEmpty(keyword))
                            result.Add(keyword);
                    }
                }
            }
            catch
            {
                // Broken materials can throw while reading keywords.
            }
#pragma warning restore 0618

            return result;
        }

