            return target == BuildTarget.Android || target == BuildTarget.iOS;
        }

        private void CreateOrUpdateProfileFromReport(bool includeReviewKeywords, bool includeUnobservedRuleKeywords)
        {
            if (report.recommendations.Count == 0)
                BuildRecommendations();

            List<string> profileStripKeywords = BuildProfileStripKeywordList(includeReviewKeywords, includeUnobservedRuleKeywords);

            if (profileStripKeywords.Count == 0)
            {
                bool canPromoteReview = report.reviewKeywords != null && report.reviewKeywords.Count > 0 && !includeReviewKeywords;
                if (canPromoteReview)
                {
                    bool promote = EditorUtility.DisplayDialog(
                        "No Recommended Strip Keywords",
                        "The scanner produced 0 recommended strip keywords, but it did produce review keywords. Do you want to promote review keywords into the generated strip profile? Review these carefully before enabling the build stripper.",
                        "Promote Review Keywords",
                        "Cancel");

                    if (!promote)
                        return;

                    profileStripKeywords = BuildProfileStripKeywordList(true, includeUnobservedRuleKeywords);
                    includeReviewKeywords = true;
                }
            }

            if (profileStripKeywords.Count == 0)
            {
                bool buildUnobserved = EditorUtility.DisplayDialog(
                    "No Keywords Available",
                    "The scanner still has 0 generated strip keywords. Create an advanced manual profile from rule keywords that were not directly observed in scan evidence? This requires validation in a development build.",
                    "Create Advanced Profile",
                    "Cancel");

                if (!buildUnobserved)
                    return;

                profileStripKeywords = BuildProfileStripKeywordList(true, true);
                includeReviewKeywords = true;
                includeUnobservedRuleKeywords = true;
            }

            ANShaderVariantAutoStripProfileV7 profile = FindOrLoadProfile(true);
            if (profile == null)
                return;

            Undo.RecordObject(profile, "Update Shader Variant Strip Profile");

            profile.stripKeywords = profileStripKeywords;
            profile.reviewKeywords = new List<string>(report.reviewKeywords);
            profile.notes = new List<string>(report.improvementNotes);
            if (includeReviewKeywords)
                profile.notes.Add("Generated with review keywords promoted into stripKeywords. Validate a development build before shipping.");
            if (includeUnobservedRuleKeywords)
                profile.notes.Add("Generated with advanced unobserved rule keywords. This is intentionally aggressive and needs manual validation.");
            profile.profileVersionHash = ComputeKeywordHash(profileStripKeywords);
            profile.generatedForBuildTarget = string.IsNullOrEmpty(report.selectedBuildTarget) ? selectedBuildTarget.ToString() : report.selectedBuildTarget;
            profile.enableBuildStripper = false; // safety default
            profile.Sanitize();

            EditorUtility.SetDirty(profile);
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();

            currentProfile = profile;
            cachedProfileSerializedObject = null;
            cachedProfileSerializedObjectTarget = null;
            Selection.activeObject = profile;

            Log("Auto strip profile created/updated: " + AssetDatabase.GetAssetPath(profile) + " | strip keywords: " + profile.stripKeywords.Count);
            EditorUtility.DisplayDialog("Profile Updated", "The auto strip profile was created/updated with " + profile.stripKeywords.Count + " strip keywords. It is disabled by default. Review the keywords, then enable Build Stripper when ready.", "OK");
        }

        private List<string> BuildProfileStripKeywordList(bool includeReviewKeywords, bool includeUnobservedRuleKeywords)
        {
            HashSet<string> result = new HashSet<string>(report.recommendedStripKeywords ?? new List<string>(), StringComparer.Ordinal);

            if (includeReviewKeywords && report.reviewKeywords != null)
                result.UnionWith(report.reviewKeywords);

            if (includeUnobservedRuleKeywords)
            {
                HashSet<string> evidence = BuildAllObservedKeywordEvidenceSet();
                foreach (FeatureRule rule in ANShaderVariantFeatureRuleDatabaseV7.Rules)
                {
                    if (rule == null || rule.keywords == null)
                        continue;

                    foreach (string keyword in rule.keywords)
                    {
                        if (!string.IsNullOrWhiteSpace(keyword) && !evidence.Contains(keyword))
                            result.Add(keyword);
                    }
                }
            }

            return result
                .Where(k => !string.IsNullOrWhiteSpace(k))
                .Select(k => k.Trim())
                .Where(k => Regex.IsMatch(k, "^[A-Za-z0-9_]+$"))
                .Distinct(StringComparer.Ordinal)
                .OrderBy(k => k, StringComparer.Ordinal)
                .ToList();
        }

        private HashSet<string> BuildAllObservedKeywordEvidenceSet()
        {
            HashSet<string> evidence = new HashSet<string>(StringComparer.Ordinal);
            evidence.UnionWith(report.materialKeywords);
            evidence.UnionWith(report.runtimeKeywordCalls);
            evidence.UnionWith(report.shaderGraphKeywordReferences);
            evidence.UnionWith(report.shaderVariantCollectionKeywords);
            evidence.UnionWith(report.vfxGraphKeywordReferences);
            evidence.UnionWith(report.renderGraphKeywordReferences);
            evidence.UnionWith(report.addressableKeywords);
            evidence.UnionWith(report.assetBundleKeywords);
            evidence.UnionWith(report.computeShaderKeywordReferences);
            if (report.volumeProfileEntries != null)
            {
                foreach (VolumeProfileEntry entry in report.volumeProfileEntries)
                {
                    if (entry == null || string.IsNullOrWhiteSpace(entry.inferredKeywords))
                        continue;

                    foreach (string keyword in entry.inferredKeywords.Split(new[] { ' ', ',', ';', '\t', '\r', '\n' }, StringSplitOptions.RemoveEmptyEntries))
                        evidence.Add(keyword.Trim());
                }
            }
            return evidence;
        }

        private ANShaderVariantAutoStripProfileV7 FindOrLoadProfile(bool createIfMissing)
        {
            string[] guids = AssetDatabase.FindAssets("t:ANShaderVariantAutoStripProfileV7");
            if (guids != null && guids.Length > 0)
            {
                string path = AssetDatabase.GUIDToAssetPath(guids[0]);
                return AssetDatabase.LoadAssetAtPath<ANShaderVariantAutoStripProfileV7>(path);
            }

            if (!createIfMissing)
                return null;

            string folder = Path.GetDirectoryName(DefaultProfilePath)?.Replace('\\', '/');
            EnsureFolder(folder);

            ANShaderVariantAutoStripProfileV7 profile = CreateInstance<ANShaderVariantAutoStripProfileV7>();
            profile.enableBuildStripper = false;
            AssetDatabase.CreateAsset(profile, DefaultProfilePath);
            AssetDatabase.SaveAssets();
            return profile;
        }

        private void EnsureFolder(string folder)
        {
            if (string.IsNullOrEmpty(folder) || AssetDatabase.IsValidFolder(folder))
                return;

            if (folder.StartsWith("Packages/", StringComparison.OrdinalIgnoreCase))
            {
                Debug.LogError("[AN Shader Variant Scanner V7] Cannot create profile inside a Packages folder: " + folder);
                return;
            }

            string[] parts = folder.Split('/');
            string current = parts[0];

            for (int i = 1; i < parts.Length; i++)
            {
                string next = current + "/" + parts[i];
                if (!AssetDatabase.IsValidFolder(next))
                    AssetDatabase.CreateFolder(current, parts[i]);
                current = next;
            }
        }

        private string[] SearchFolders()
        {
