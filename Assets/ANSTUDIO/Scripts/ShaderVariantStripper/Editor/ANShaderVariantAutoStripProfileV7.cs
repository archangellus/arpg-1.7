            public void ApplyTo(ScanReport report)
            {
                report.Clear();
                report.lastScanTime = lastScanTime ?? "";
                report.selectedBuildTarget = selectedBuildTarget ?? "";
                AddRange(report.materialPaths, materialPaths);
                AddRange(report.shaderNames, shaderNames);
                AddRange(report.materialKeywords, materialKeywords);
                AddRange(report.runtimeKeywordCalls, runtimeKeywordCalls);
                AddRange(report.runtimeKeywordSourcePaths, runtimeKeywordSourcePaths);
                AddRange(report.shaderGraphKeywordReferences, shaderGraphKeywordReferences);
                AddRange(report.shaderGraphKeywordSourcePaths, shaderGraphKeywordSourcePaths);
                AddRange(report.shaderVariantCollectionKeywords, shaderVariantCollectionKeywords);
                AddRange(report.shaderVariantCollectionSourcePaths, shaderVariantCollectionSourcePaths);
                AddRange(report.vfxGraphKeywordReferences, vfxGraphKeywordReferences);
                AddRange(report.vfxGraphKeywordSourcePaths, vfxGraphKeywordSourcePaths);
                AddRange(report.renderGraphKeywordReferences, renderGraphKeywordReferences);
                AddRange(report.renderGraphKeywordSourcePaths, renderGraphKeywordSourcePaths);
                AddRange(report.computeShaderKeywordReferences, computeShaderKeywordReferences);
                AddRange(report.computeShaderSourcePaths, computeShaderSourcePaths);
                AddRange(report.globalSettingsPaths, globalSettingsPaths);
                AddRange(report.graphicsApiNames, graphicsApiNames);
                report.graphicsApiNotes = graphicsApiNotes == null ? new List<string>() : new List<string>(graphicsApiNotes);
                report.urpStripSettingsEntries = urpStripSettingsEntries == null ? new List<UrpStripSettingsEntry>() : new List<UrpStripSettingsEntry>(urpStripSettingsEntries);
                report.urpBuiltInStripNotes = urpBuiltInStripNotes == null ? new List<string>() : new List<string>(urpBuiltInStripNotes);
                report.rendererDataEntries = rendererDataEntries == null ? new List<RendererDataEntry>() : new List<RendererDataEntry>(rendererDataEntries);
                report.volumeProfileEntries = volumeProfileEntries == null ? new List<VolumeProfileEntry>() : new List<VolumeProfileEntry>(volumeProfileEntries);
                report.alwaysIncludedShaderEntries = alwaysIncludedShaderEntries == null ? new List<AlwaysIncludedShaderEntry>() : new List<AlwaysIncludedShaderEntry>(alwaysIncludedShaderEntries);
                report.apvStreamingEntries = apvStreamingEntries == null ? new List<ApvStreamingEntry>() : new List<ApvStreamingEntry>(apvStreamingEntries);
                report.srpBatcherEntries = srpBatcherEntries == null ? new List<SRPBatcherEntry>() : new List<SRPBatcherEntry>(srpBatcherEntries);
                AddRange(report.addressableGroupAssetPaths, addressableGroupAssetPaths);
                AddRange(report.addressableAssetPaths, addressableAssetPaths);
                AddRange(report.addressableMaterialPaths, addressableMaterialPaths);
                AddRange(report.addressableKeywords, addressableKeywords);
                AddRange(report.assetBundleAssetPaths, assetBundleAssetPaths);
                AddRange(report.assetBundleMaterialPaths, assetBundleMaterialPaths);
                AddRange(report.assetBundleKeywords, assetBundleKeywords);
                AddRange(report.scannedScenes, scannedScenes);
                AddRange(report.skippedScenes, skippedScenes);
                AddRange(report.pipelineAssetPaths, pipelineAssetPaths);
                AddRange(report.rendererFeatures, rendererFeatures);
                AddRange(report.detectedFeatures, detectedFeatures);
                AddRange(report.detectedFeatureIds, detectedFeatureIds);

                report.recommendedStripKeywords = recommendedStripKeywords == null ? new List<string>() : new List<string>(recommendedStripKeywords);
                report.reviewKeywords = reviewKeywords == null ? new List<string>() : new List<string>(reviewKeywords);
                report.improvementNotes = improvementNotes == null ? new List<string>() : new List<string>(improvementNotes);
                report.shaderBudgetEntries = shaderBudgetEntries == null ? new List<ShaderBudgetEntry>() : new List<ShaderBudgetEntry>(shaderBudgetEntries);
                report.dryRunEntries = dryRunEntries == null ? new List<DryRunStripEntry>() : new List<DryRunStripEntry>(dryRunEntries);
                report.log = log == null ? new List<string>() : new List<string>(log);

                report.recommendations.Clear();
                if (recommendations != null)
                {
                    foreach (PersistedRecommendation persistedRecommendation in recommendations)
                    {
                        FeatureRule rule = FindRuleById(persistedRecommendation.ruleId);
                        if (rule == null)
                            continue;

                        report.recommendations.Add(new Recommendation
                        {
                            rule = rule,
                            action = persistedRecommendation.action,
                            reason = persistedRecommendation.reason,
                            targetSummary = persistedRecommendation.targetSummary,
                            unityBuiltInStripNote = persistedRecommendation.unityBuiltInStripNote
                        });
                    }
                }
            }

            private static void AddRange(HashSet<string> target, IEnumerable<string> values)
            {
                if (target == null || values == null)
                    return;

                foreach (string value in values)
                {
                    if (!string.IsNullOrEmpty(value))
                        target.Add(value);
                }
            }
