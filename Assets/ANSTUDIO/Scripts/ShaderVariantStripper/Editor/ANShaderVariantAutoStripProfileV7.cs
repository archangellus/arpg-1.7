        private void ScanAddressableGroupAssets()
        {
            HashSet<string> addressableCandidatePaths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
            string[] filters = { "t:AddressableAssetSettings", "t:AddressableAssetGroup" };

            foreach (string filter in filters)
            {
                string[] guids;
                try
                {
                    guids = AssetDatabase.FindAssets(filter, SearchFolders());
                }
                catch
                {
                    continue;
                }

                for (int i = 0; i < guids.Length; i++)
                {
                    if (!Progress("Scanning Addressables group assets", $"{filter} {i + 1}/{guids.Length}", i, guids.Length))
                        return;

                    string groupPath = AssetDatabase.GUIDToAssetPath(guids[i]);
                    if (!ShouldScanAssetPath(groupPath))
                        continue;

                    report.addressableGroupAssetPaths.Add(groupPath);

                    UnityEngine.Object groupAsset = AssetDatabase.LoadAssetAtPath<UnityEngine.Object>(groupPath);
                    foreach (string referencedGuid in CollectGuidStringsFromAsset(groupAsset, groupPath))
                    {
                        string assetPath = AssetDatabase.GUIDToAssetPath(referencedGuid);
                        if (string.IsNullOrEmpty(assetPath) || !ShouldScanAssetPath(assetPath))
                            continue;

                        addressableCandidatePaths.Add(assetPath);
                    }
                }
            }

            int index = 0;
            foreach (string assetPath in addressableCandidatePaths.OrderBy(p => p, StringComparer.OrdinalIgnoreCase))
            {
                if (!Progress("Scanning Addressable dependencies", $"{index + 1}/{addressableCandidatePaths.Count}: {assetPath}", index, addressableCandidatePaths.Count))
                    return;

                report.addressableAssetPaths.Add(assetPath);
                ScanExternalAssetDependencies(assetPath, report.addressableMaterialPaths, report.addressableKeywords, "Addressables");
                index++;
            }

            if (report.addressableGroupAssetPaths.Count > 0)
                Log($"Addressables scan found {report.addressableAssetPaths.Count} candidate assets and {report.addressableKeywords.Count} keywords.");
        }


