                   rule.id == "SSAO" ||
                   rule.id == "DECALS" ||
                   rule.id == "REFLECTION_PROBES" ||
                   rule.id == "MOTION_VECTORS" ||
                   rule.id == "LOD_CROSSFADE" ||
                   rule.id == "RENDER_PASS" ||
                   rule.id == "APV" ||
                   rule.id == "SCREEN_SPACE_LENS_FLARE" ||
                   rule.id == "GPU_RESIDENT_DRAWER" ||
                   rule.id == "POST_PROCESS_AA" ||
                   rule.id == "TEMPORAL_UPSCALING" ||
                   rule.id == "SPEEDTREE";
        }

