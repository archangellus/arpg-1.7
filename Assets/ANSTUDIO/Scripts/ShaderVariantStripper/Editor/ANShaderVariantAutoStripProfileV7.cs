        private string CollectFeatureSummaryFromSerializedObject(UnityEngine.Object obj)
        {
            if (obj == null)
                return "None";

            List<string> hints = new List<string>();
            try
            {
                SerializedObject serializedObject = new SerializedObject(obj);
                SerializedProperty iterator = serializedObject.GetIterator();
                int guard = 0;
                while (iterator.NextVisible(true))
                {
                    if (++guard > 2000)
                        break;

                    if ((iterator.propertyType == SerializedPropertyType.Boolean && iterator.boolValue) ||
                        (iterator.propertyType == SerializedPropertyType.Integer && iterator.intValue != 0) ||
                        (iterator.propertyType == SerializedPropertyType.String && !string.IsNullOrEmpty(iterator.stringValue)))
                    {
                        string combined = ((iterator.displayName ?? "") + " " + (iterator.name ?? "") + " " + (iterator.propertyType == SerializedPropertyType.String ? iterator.stringValue : "")).ToLowerInvariant();
                        if (combined.Contains("additional") && combined.Contains("light")) hints.Add("Additional Lights");
                        if (combined.Contains("main") && combined.Contains("shadow")) hints.Add("Main Shadows");
                        if (combined.Contains("soft") && combined.Contains("shadow")) hints.Add("Soft Shadows");
                        if (combined.Contains("decal")) hints.Add("Decals");
                        if (combined.Contains("ambientocclusion") || combined.Contains("ambient occlusion")) hints.Add("SSAO");
                        if (combined.Contains("probevolume") || combined.Contains("adaptive probe")) hints.Add("APV");
                        if (combined.Contains("gpu resident") || combined.Contains("gpuresident") || combined.Contains("batchrenderergroup")) hints.Add("GPU Resident Drawer");
                        if (combined.Contains("water")) hints.Add("Water");
                        if (combined.Contains("rendergraph") || combined.Contains("render graph")) hints.Add("Render Graph");
                    }
                }
            }
            catch
            {
                return "Unable to read serialized settings";
            }

            return hints.Count == 0 ? "No major variant-driving features detected from serialized fields" : string.Join(", ", hints.Distinct().OrderBy(h => h, StringComparer.Ordinal));
        }

        private static string ComputeKeywordHash(IEnumerable<string> keywords)
        {
            string joined = string.Join("\n", (keywords ?? Enumerable.Empty<string>()).Where(k => !string.IsNullOrWhiteSpace(k)).Select(k => k.Trim()).Distinct(StringComparer.Ordinal).OrderBy(k => k, StringComparer.Ordinal));
            using (SHA256 sha = SHA256.Create())
            {
                byte[] bytes = sha.ComputeHash(Encoding.UTF8.GetBytes(joined));
                return BitConverter.ToString(bytes).Replace("-", "").ToLowerInvariant();
            }
        }


