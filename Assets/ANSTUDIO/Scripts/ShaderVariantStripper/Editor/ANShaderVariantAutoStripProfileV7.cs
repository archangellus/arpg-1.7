        private string CountDelta<T>(ICollection<T> a, ICollection<T> b)
        {
            int av = a == null ? 0 : a.Count;
            int bv = b == null ? 0 : b.Count;
            int delta = bv - av;
            return av + " → " + bv + " (" + (delta >= 0 ? "+" : "") + delta + ")";
        }

