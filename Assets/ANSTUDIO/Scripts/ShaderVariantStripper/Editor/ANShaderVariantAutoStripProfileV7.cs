        private IEnumerable<Recommendation> FilteredRecommendations()
        {
            if (string.IsNullOrWhiteSpace(searchFilter))
                return report.recommendations;

            string filter = searchFilter.Trim();
            return report.recommendations.Where(r =>
                r.rule.displayName.IndexOf(filter, StringComparison.OrdinalIgnoreCase) >= 0 ||
                r.reason.IndexOf(filter, StringComparison.OrdinalIgnoreCase) >= 0 ||
                (!string.IsNullOrEmpty(r.targetSummary) && r.targetSummary.IndexOf(filter, StringComparison.OrdinalIgnoreCase) >= 0) ||
                r.rule.keywords.Any(k => k.IndexOf(filter, StringComparison.OrdinalIgnoreCase) >= 0));
        }

