        private string BuildRecommendationAutoApplyStatus(Recommendation rec, bool applied, string keywordPreview)
        {
            if (rec == null || rec.rule == null)
                return "No recommendation data is available.";

            if (rec.action == RecommendationAction.Keep)
                return "Usage was detected, so the normal automatic apply path is blocked. Use Force Apply only if you intentionally want to override that evidence for this build target. Keywords: " + keywordPreview;

            if (applied)
                return "This recommendation is already active in the auto-strip profile. Force Enable can re-enable the build stripper even if validation would normally block it. Keywords: " + keywordPreview;

            if (rec.action == RecommendationAction.Review)
                return "This is a review recommendation. Normal automatic application requires confirmation. Force Apply bypasses the normal review/validation guardrails and should only be used after manual verification. Keywords: " + keywordPreview;

            return "Applies only this recommendation immediately to the auto-strip profile, then validates and simulates. Force Apply is available as an override if validation blocks a change you intentionally want. Keywords: " + keywordPreview;
        }

        private bool IsRecommendationAppliedToCurrentProfile(Recommendation rec)
        {
            if (rec == null || rec.rule == null || rec.rule.keywords == null || rec.rule.keywords.Length == 0)
                return false;

            ANShaderVariantAutoStripProfileV7 profile = currentProfile;
            if (profile == null)
                profile = FindOrLoadProfile(false);

            if (profile == null || profile.stripKeywords == null)
                return false;

            HashSet<string> profileKeywords = new HashSet<string>(profile.stripKeywords.Where(k => !string.IsNullOrWhiteSpace(k)), StringComparer.Ordinal);
            return rec.rule.keywords.Where(k => !string.IsNullOrWhiteSpace(k)).All(k => profileKeywords.Contains(k.Trim()));
        }

        private void ApplyRecommendationAutomatically(Recommendation rec)
        {
            if (rec == null || rec.rule == null)
                return;

            if (rec.action == RecommendationAction.Keep)
            {
                EditorUtility.DisplayDialog("Cannot Apply Automatically", "This recommendation is marked Keep because usage was detected. Fix the source usage first, run the scanner again, then apply it only if it becomes a strip or review candidate.", "OK");
                return;
            }

            if (rec.action == RecommendationAction.Review)
            {
                bool proceed = EditorUtility.DisplayDialog(
                    "Apply Review Recommendation?",
                    "This recommendation is marked Review, not a normal strip candidate. Apply it only if you have checked scenes, Addressables, AssetBundles, renderer data, volume profiles and runtime keyword usage for this build target.\n\nFeature: " + rec.rule.displayName + "\nKeywords: " + string.Join(", ", rec.rule.keywords),
                    "Apply Anyway",
                    "Cancel");

                if (!proceed)
                    return;
            }

            List<string> keywords = BuildRecommendationKeywordList(rec);
            if (keywords.Count == 0)
            {
                EditorUtility.DisplayDialog("No Keywords", "This recommendation has no valid shader keywords to apply.", "OK");
                return;
            }

            ANShaderVariantAutoStripProfileV7 profile = FindOrLoadProfile(true);
            if (profile == null)
                return;

            Undo.RecordObject(profile, "Apply Shader Variant Recommendation");

            HashSet<string> merged = new HashSet<string>(profile.stripKeywords ?? new List<string>(), StringComparer.Ordinal);
            foreach (string keyword in keywords)
                merged.Add(keyword);

            profile.stripKeywords = merged.OrderBy(k => k, StringComparer.Ordinal).ToList();
            UpdateProfileMetadataAfterRecommendationChange(profile, rec, "Auto-applied");
            profile.Sanitize();

            EditorUtility.SetDirty(profile);
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();

            currentProfile = profile;
            cachedProfileSerializedObject = null;
            cachedProfileSerializedObjectTarget = null;
            Selection.activeObject = profile;

            bool valid = ValidateCurrentProfileAndReport(false);
            if (valid)
            {
                profile.enableBuildStripper = true;
                EditorUtility.SetDirty(profile);
                AssetDatabase.SaveAssets();
                SimulateStripWithCurrentProfile();
                Log("Auto-applied recommendation: " + rec.rule.displayName + " | keywords: " + string.Join(", ", keywords));
                EditorUtility.DisplayDialog("Recommendation Applied", "Applied " + rec.rule.displayName + " to the auto-strip profile and enabled the build stripper. A simulation report was generated in the Auto Stripper tab.", "OK");
            }
            else
            {
                profile.enableBuildStripper = false;
                EditorUtility.SetDirty(profile);
                AssetDatabase.SaveAssets();
                SimulateStripWithCurrentProfile();
                Log("Auto-applied recommendation but validation blocked enabling: " + rec.rule.displayName + " | keywords: " + string.Join(", ", keywords));
                EditorUtility.DisplayDialog("Applied, But Not Enabled", "The keywords were added to the profile, but validation did not pass, so the build stripper was not enabled. Check the Auto Stripper tab for the validation message.", "OK");
            }

            SaveLastReportToDisk();
            Repaint();
        }

        private void ForceApplyRecommendationAutomatically(Recommendation rec)
        {
            if (rec == null || rec.rule == null)
                return;

            List<string> keywords = BuildRecommendationKeywordList(rec);
            if (keywords.Count == 0)
            {
                EditorUtility.DisplayDialog("No Keywords", "This recommendation has no valid shader keywords to force apply.", "OK");
                return;
            }

            string riskText = rec.rule.risk.ToString().ToUpperInvariant();
            bool proceed = EditorUtility.DisplayDialog(
                "Force Apply Recommendation?",
                "Force Apply bypasses the normal safety blocks for this single recommendation.\n\n" +
                "It will add these keywords to the auto-strip profile and enable the build stripper even if validation reports that loaded materials still use them.\n\n" +
                "Feature: " + rec.rule.displayName + "\n" +
                "Action: " + rec.action + "\n" +
                "Risk: " + riskText + "\n" +
                "Keywords: " + string.Join(", ", keywords) + "\n\n" +
                "Use this only when you have intentionally verified the target build does not need these variants, or when you are making a test/development build.",
                "Force Apply",
                "Cancel");

            if (!proceed)
                return;

            ANShaderVariantAutoStripProfileV7 profile = FindOrLoadProfile(true);
            if (profile == null)
                return;

            Undo.RecordObject(profile, "Force Apply Shader Variant Recommendation");

            HashSet<string> merged = new HashSet<string>(profile.stripKeywords ?? new List<string>(), StringComparer.Ordinal);
            foreach (string keyword in keywords)
                merged.Add(keyword);

            profile.stripKeywords = merged.OrderBy(k => k, StringComparer.Ordinal).ToList();
            UpdateProfileMetadataAfterRecommendationChange(profile, rec, "Force-applied");

            if (profile.notes == null)
                profile.notes = new List<string>();

            string forceNote = "FORCE APPLIED recommendation: " + rec.rule.displayName + " | Risk: " + riskText + " | Keywords: " + string.Join(", ", keywords);
            if (!profile.notes.Contains(forceNote))
                profile.notes.Add(forceNote);

            profile.Sanitize();

            EditorUtility.SetDirty(profile);
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();

            currentProfile = profile;
            cachedProfileSerializedObject = null;
            cachedProfileSerializedObjectTarget = null;
            Selection.activeObject = profile;

            bool valid = ValidateCurrentProfileAndReport(false);

            // Force Apply means the user intentionally overrides validation blocking.
            // ValidateCurrentProfileAndReport can disable the stripper on failure, so re-enable it here.
            profile.enableBuildStripper = true;

            if (!valid)
            {
                string validationNote = "FORCE ENABLED despite validation warning: " + lastProfileValidationMessage;
                if (!string.IsNullOrWhiteSpace(validationNote) && !profile.notes.Contains(validationNote))
                    profile.notes.Add(validationNote);
            }

            EditorUtility.SetDirty(profile);
            AssetDatabase.SaveAssets();
            SimulateStripWithCurrentProfile();

            string resultMessage = valid
                ? "Force applied " + rec.rule.displayName + ". Validation passed, the build stripper is enabled, and a simulation report was generated."
                : "Force applied " + rec.rule.displayName + ". Validation did not pass, but the build stripper was enabled because you used Force Apply. Check the Auto Stripper tab before making a release build.";

            Log("Force-applied recommendation: " + rec.rule.displayName + " | validation passed: " + valid + " | keywords: " + string.Join(", ", keywords));
            SaveLastReportToDisk();
            Repaint();

            EditorUtility.DisplayDialog("Force Apply Complete", resultMessage, "OK");
        }

        private void RemoveRecommendationFromProfile(Recommendation rec)
        {
            if (rec == null || rec.rule == null)
                return;

            ANShaderVariantAutoStripProfileV7 profile = FindOrLoadProfile(false);
            if (profile == null || profile.stripKeywords == null || profile.stripKeywords.Count == 0)
                return;

            List<string> keywords = BuildRecommendationKeywordList(rec);
            if (keywords.Count == 0)
                return;

            bool proceed = EditorUtility.DisplayDialog(
                "Remove Recommendation From Profile?",
                "Remove only these keywords from the auto-strip profile?\n\nFeature: " + rec.rule.displayName + "\nKeywords: " + string.Join(", ", keywords),
                "Remove",
                "Cancel");

            if (!proceed)
                return;

            Undo.RecordObject(profile, "Remove Shader Variant Recommendation");

            HashSet<string> remove = new HashSet<string>(keywords, StringComparer.Ordinal);
            profile.stripKeywords = profile.stripKeywords
                .Where(k => !string.IsNullOrWhiteSpace(k) && !remove.Contains(k.Trim()))
                .Distinct(StringComparer.Ordinal)
                .OrderBy(k => k, StringComparer.Ordinal)
                .ToList();

            UpdateProfileMetadataAfterRecommendationChange(profile, rec, "Removed auto-applied");
            profile.Sanitize();

            if (profile.stripKeywords.Count == 0)
                profile.enableBuildStripper = false;

            EditorUtility.SetDirty(profile);
            AssetDatabase.SaveAssets();
            AssetDatabase.Refresh();

            currentProfile = profile;
            cachedProfileSerializedObject = null;
            cachedProfileSerializedObjectTarget = null;
            Selection.activeObject = profile;

            if (profile.stripKeywords.Count > 0)
                ValidateCurrentProfileAndReport(false);
            SimulateStripWithCurrentProfile();

            Log("Removed recommendation from profile: " + rec.rule.displayName + " | keywords: " + string.Join(", ", keywords));
            SaveLastReportToDisk();
            Repaint();
        }

        private List<string> BuildRecommendationKeywordList(Recommendation rec)
        {
            if (rec == null || rec.rule == null || rec.rule.keywords == null)
                return new List<string>();

            return rec.rule.keywords
                .Where(k => !string.IsNullOrWhiteSpace(k))
                .Select(k => k.Trim())
                .Where(k => Regex.IsMatch(k, "^[A-Za-z0-9_]+$"))
                .Distinct(StringComparer.Ordinal)
                .OrderBy(k => k, StringComparer.Ordinal)
                .ToList();
        }

        private void UpdateProfileMetadataAfterRecommendationChange(ANShaderVariantAutoStripProfileV7 profile, Recommendation rec, string actionLabel)
        {
            if (profile == null)
                return;

            if (profile.reviewKeywords == null)
                profile.reviewKeywords = new List<string>();
            if (report.reviewKeywords != null)
                profile.reviewKeywords = report.reviewKeywords.Distinct(StringComparer.Ordinal).OrderBy(k => k, StringComparer.Ordinal).ToList();

            if (profile.notes == null)
                profile.notes = new List<string>();

            if (report.improvementNotes != null)
            {
                foreach (string note in report.improvementNotes)
                {
                    if (!string.IsNullOrWhiteSpace(note) && !profile.notes.Contains(note))
                        profile.notes.Add(note);
                }
            }

            if (rec != null && rec.rule != null)
            {
                string note = actionLabel + " recommendation: " + rec.rule.displayName + " | " + string.Join(", ", BuildRecommendationKeywordList(rec));
                if (!profile.notes.Contains(note))
                    profile.notes.Add(note);
            }

            profile.profileVersionHash = ComputeKeywordHash(profile.stripKeywords);
            profile.generatedForBuildTarget = string.IsNullOrEmpty(report.selectedBuildTarget) ? selectedBuildTarget.ToString() : report.selectedBuildTarget;
        }

        private void DrawStepInstructionTextSizeControls(Recommendation rec)
        {
            int currentSize = GetStepInstructionFontSize(rec);

            using (new EditorGUILayout.HorizontalScope(EditorStyles.toolbar))
            {
                EditorGUILayout.LabelField("Step text size", EditorStyles.miniBoldLabel, GUILayout.Width(96));

                using (new EditorGUI.DisabledScope(currentSize <= StepInstructionMinFontSize))
                {
                    if (GUILayout.Button("A-", EditorStyles.toolbarButton, GUILayout.Width(34)))
                        SetStepInstructionFontSize(rec, currentSize - 1);
                }

                int editedSize = EditorGUILayout.IntField(currentSize, GUILayout.Width(44));
                if (editedSize != currentSize)
                {
                    SetStepInstructionFontSize(rec, editedSize);
                    currentSize = GetStepInstructionFontSize(rec);
                }

                using (new EditorGUI.DisabledScope(currentSize >= StepInstructionMaxFontSize))
                {
                    if (GUILayout.Button("A+", EditorStyles.toolbarButton, GUILayout.Width(34)))
                        SetStepInstructionFontSize(rec, currentSize + 1);
                }

                if (GUILayout.Button("Reset", EditorStyles.toolbarButton, GUILayout.Width(54)))
                    SetStepInstructionFontSize(rec, StepInstructionDefaultFontSize);

                GUILayout.Space(6);
                EditorGUILayout.LabelField("per recommendation", EditorStyles.miniLabel, GUILayout.Width(120));
                GUILayout.FlexibleSpace();
            }
        }

        private void DrawStepInstructionTextBox(string text, StripRisk risk, int fontSize)
        {
            GUIStyle style = new GUIStyle(EditorStyles.helpBox);
            style.wordWrap = true;
            style.richText = false;
            style.fontSize = Mathf.Clamp(fontSize, StepInstructionMinFontSize, StepInstructionMaxFontSize);
            style.padding = new RectOffset(10, 10, 8, 8);
            Color stepTextColor = GetStepInstructionTextColor();
            style.normal.textColor = stepTextColor;
            style.hover.textColor = stepTextColor;
            style.active.textColor = stepTextColor;
            style.focused.textColor = stepTextColor;

            GUIContent content = new GUIContent(text ?? string.Empty);
            float width = Mathf.Max(240f, EditorGUIUtility.currentViewWidth - 92f);
            float height = Mathf.Max(52f, style.CalcHeight(content, width) + 6f);
            Rect rect = GUILayoutUtility.GetRect(content, style, GUILayout.ExpandWidth(true), GUILayout.MinHeight(height));

            EditorGUI.DrawRect(new Rect(rect.x, rect.y, 5f, rect.height), GetRiskStepHeaderColor(risk));
            GUI.Label(new Rect(rect.x + 6f, rect.y, rect.width - 6f, rect.height), content, style);
        }

        private int GetStepInstructionFontSize(Recommendation rec)
        {
            string key = GetStepInstructionFontSizeKey(rec);
            if (string.IsNullOrEmpty(key))
                return StepInstructionDefaultFontSize;

            int cached;
            if (stepInstructionFontSizesByRule.TryGetValue(key, out cached))
                return Mathf.Clamp(cached, StepInstructionMinFontSize, StepInstructionMaxFontSize);

            int saved = EditorPrefs.GetInt(GetStepInstructionEditorPrefsKey(key), StepInstructionDefaultFontSize);
            saved = Mathf.Clamp(saved, StepInstructionMinFontSize, StepInstructionMaxFontSize);
            stepInstructionFontSizesByRule[key] = saved;
            return saved;
        }

        private void SetStepInstructionFontSize(Recommendation rec, int size)
        {
            string key = GetStepInstructionFontSizeKey(rec);
            if (string.IsNullOrEmpty(key))
                return;

            int clamped = Mathf.Clamp(size, StepInstructionMinFontSize, StepInstructionMaxFontSize);
            stepInstructionFontSizesByRule[key] = clamped;
            EditorPrefs.SetInt(GetStepInstructionEditorPrefsKey(key), clamped);
            Repaint();
        }

        private string GetStepInstructionFontSizeKey(Recommendation rec)
        {
            if (rec == null || rec.rule == null)
                return string.Empty;

            if (!string.IsNullOrEmpty(rec.rule.id))
                return rec.rule.id;

            return rec.rule.displayName ?? string.Empty;
        }

        private string GetStepInstructionEditorPrefsKey(string key)
        {
            return "ANSTUDIO.ShaderVariantScannerV7.StepInstructionFontSize." + key;
        }

        private Color GetStepInstructionTextColor()
        {
            // Brighter step text for dark editor skin, while preserving readability in light skin.
            return EditorGUIUtility.isProSkin ? new Color(0.94f, 0.94f, 0.94f, 1f) : new Color(0.12f, 0.12f, 0.12f, 1f);
        }

        private Color GetRiskAccentColor(StripRisk risk)
        {
            switch (risk)
            {
                case StripRisk.Low:
                    return new Color(0.18f, 0.62f, 0.28f, 1f);
                case StripRisk.Medium:
                    return new Color(1.00f, 0.62f, 0.12f, 1f);
                case StripRisk.High:
                    return new Color(0.82f, 0.18f, 0.16f, 1f);
                default:
                    return EditorGUIUtility.isProSkin ? new Color(0.35f, 0.35f, 0.35f, 1f) : new Color(0.70f, 0.70f, 0.70f, 1f);
            }
        }

        private Color GetRiskStepHeaderColor(StripRisk risk)
        {
            Color accent = GetRiskAccentColor(risk);
            if (risk == StripRisk.Medium)
                return new Color(0.92f, 0.47f, 0.05f, 1f);

            return new Color(Mathf.Clamp01(accent.r * 0.85f), Mathf.Clamp01(accent.g * 0.85f), Mathf.Clamp01(accent.b * 0.85f), 1f);
        }

        private Color GetRiskHeaderTextColor(StripRisk risk)
        {
            return risk == StripRisk.Medium ? Color.black : Color.white;
        }

