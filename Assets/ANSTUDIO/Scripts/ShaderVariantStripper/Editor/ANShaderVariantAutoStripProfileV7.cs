        private bool ValidateCurrentProfileAndReport(bool showDialog)
        {
            if (currentProfile == null)
            {
                lastProfileValidationMessage = "No profile is assigned.";
                lastProfileValidationType = MessageType.Error;
                if (showDialog)
                    EditorUtility.DisplayDialog("Profile Validation Failed", lastProfileValidationMessage, "OK");
                return false;
            }

            currentProfile.Sanitize();

            if (currentProfile.stripKeywords == null || currentProfile.stripKeywords.Count == 0)
            {
                lastProfileValidationMessage = "The profile has no strip keywords. Build stripping cannot be enabled until the generated keyword list is non-empty.";
                lastProfileValidationType = MessageType.Error;
                currentProfile.enableBuildStripper = false;
                EditorUtility.SetDirty(currentProfile);
                if (showDialog)
                    EditorUtility.DisplayDialog("Profile Validation Failed", lastProfileValidationMessage, "OK");
                return false;
            }

            HashSet<string> loadedMaterialKeywords = new HashSet<string>(StringComparer.Ordinal);
            Material[] loadedMaterials = Resources.FindObjectsOfTypeAll<Material>();

            foreach (Material material in loadedMaterials)
            {
                if (material == null)
                    continue;

                foreach (string keyword in ReadMaterialKeywordNames(material))
                    loadedMaterialKeywords.Add(keyword);
            }

            List<string> conflicts = currentProfile.stripKeywords
                .Where(k => loadedMaterialKeywords.Contains(k))
                .Distinct(StringComparer.Ordinal)
                .OrderBy(k => k, StringComparer.Ordinal)
                .ToList();

            if (conflicts.Count > 0)
            {
                lastProfileValidationMessage =
                    "Build stripper was not enabled because these strip keywords appear on currently loaded materials: " +
                    string.Join(", ", conflicts.Take(24)) +
                    (conflicts.Count > 24 ? $" ... and {conflicts.Count - 24} more." : "");

                lastProfileValidationType = MessageType.Error;
                currentProfile.enableBuildStripper = false;
                EditorUtility.SetDirty(currentProfile);

                if (showDialog)
                    EditorUtility.DisplayDialog("Profile Validation Failed", lastProfileValidationMessage, "OK");

                return false;
            }

            lastProfileValidationMessage = $"Profile validation passed. {currentProfile.stripKeywords.Count} strip keywords are configured and none were found on currently loaded materials.";
            lastProfileValidationType = MessageType.Info;

            if (showDialog)
                EditorUtility.DisplayDialog("Profile Validation Passed", lastProfileValidationMessage, "OK");

            return true;
        }


