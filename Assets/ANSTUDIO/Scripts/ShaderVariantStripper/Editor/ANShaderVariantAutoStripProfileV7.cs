        private void ScanMaterial(Material material, string path)
        {
            if (material == null)
                return;

            if (!includePackagesInKeywordResult && IsPackagePath(path))
                return;

            report.materialPaths.Add(path);

            if (material.shader != null)
                report.shaderNames.Add(material.shader.name);

            AddMaterialKeywords(material, path);
            DetectMaterialFeatureEvidence(material, path);
        }

        private void AddMaterialKeywords(Material material, string path)
        {
            if (material == null)
                return;

            try
            {
                LocalKeyword[] enabled = material.enabledKeywords;
                if (enabled != null)
                {
                    foreach (LocalKeyword keyword in enabled)
                    {
                        if (!string.IsNullOrEmpty(keyword.name))
                        {
                            report.materialKeywords.Add(keyword.name);
                            DetectFeatureFromKeyword(keyword.name, "Material keyword: " + path);
                        }
                    }
                }
            }
            catch
            {
                // Fallback below.
            }

#pragma warning disable 0618
            try
            {
                string[] keywords = material.shaderKeywords;
                if (keywords != null)
                {
                    foreach (string keyword in keywords)
                    {
                        if (!string.IsNullOrEmpty(keyword))
                        {
                            report.materialKeywords.Add(keyword);
                            DetectFeatureFromKeyword(keyword, "Material keyword fallback: " + path);
                        }
                    }
                }
            }
            catch
            {
                // Some broken materials can throw while reading keywords.
            }
#pragma warning restore 0618
        }

        private void DetectMaterialFeatureEvidence(Material material, string path)
        {
            if (material == null)
                return;

            try
            {
                if (material.IsKeywordEnabled("_EMISSION"))
                    report.detectedFeatures.Add("Emission materials: " + path);

                if (material.enableInstancing)
                    MarkFeature("INSTANCING", "Material has GPU instancing enabled: " + path);

                if (material.HasProperty("_BumpMap") && material.GetTexture("_BumpMap") != null)
                    report.detectedFeatures.Add("Normal maps: " + path);

                if (material.HasProperty("_ParallaxMap") && material.GetTexture("_ParallaxMap") != null)
                    report.detectedFeatures.Add("Parallax maps: " + path);

                if (material.shader != null && !string.IsNullOrEmpty(material.shader.name) &&
                    material.shader.name.IndexOf("SpeedTree", StringComparison.OrdinalIgnoreCase) >= 0)
                {
                    MarkFeature("SPEEDTREE", "Material uses SpeedTree shader: " + path);
                    foreach (string keyword in ReadMaterialKeywordNames(material))
                    {
                        if (keyword == "EFFECT_BUMP" || keyword == "EFFECT_HUE_VARIATION" || keyword == "EFFECT_SUBSURFACE" || keyword == "ENABLE_WIND")
                            report.materialKeywords.Add(keyword);
                    }
                }
            }
            catch
            {
                // Material property access can fail for missing shaders.
            }
        }

        private void ScanRenderer(Renderer renderer, string sourcePath)
        {
            if (renderer == null)
                return;

            try
            {
                Material[] materials = renderer.sharedMaterials;
                if (materials != null)
                {
                    foreach (Material material in materials)
                    {
                        if (material == null)
                            continue;

                        string path = AssetDatabase.GetAssetPath(material);
                        if (string.IsNullOrEmpty(path))
                            path = sourcePath + " (scene material reference)";

                        ScanMaterial(material, path);
                    }
                }

                if (renderer.allowOcclusionWhenDynamic == false)
                    report.detectedFeatures.Add("Renderer disables dynamic occlusion: " + sourcePath);
            }
            catch
            {
                // Missing renderer references are ignored.
            }
        }

        private void ScanLight(Light light, string sourcePath)
        {
            if (light == null || !light.enabled)
                return;

            if (light.type != LightType.Directional)
                MarkFeature("ADDITIONAL_LIGHTS", $"Scene has enabled {light.type} light: {sourcePath}");

            if (light.shadows != LightShadows.None)
            {
                if (light.type == LightType.Directional)
                    MarkFeature("MAIN_LIGHT_SHADOWS", "Directional light casts realtime shadows: " + sourcePath);
                else
                    MarkFeature("ADDITIONAL_LIGHT_SHADOWS", $"Additional {light.type} light casts shadows: {sourcePath}");

                if (light.shadows == LightShadows.Soft)
                    MarkFeature("SOFT_SHADOWS", "Light uses soft shadows: " + sourcePath);
            }

            if (light.cookie != null)
                MarkFeature("LIGHT_COOKIES", "Light has a cookie texture: " + sourcePath);
        }

        private void ScanComponents(Component[] components, string sourcePath)
        {
            ScanComponents(components, sourcePath, -1f, 0f, null);
        }

        private void ScanComponents(Component[] components, string sourcePath, float progressBase, float progressSpan, string progressTitle)
        {
            if (components == null)
                return;

            for (int i = 0; i < components.Length; i++)
            {
                if (progressBase >= 0f && (i & 31) == 0)
                {
                    string title = string.IsNullOrEmpty(progressTitle) ? "Scanning components" : "Scanning scene components";
                    if (!ProgressValue(title, $"{sourcePath} | component {i + 1}/{components.Length}", progressBase + progressSpan * (components.Length <= 0 ? 1f : i / (float)components.Length)))
                        return;
                }

                Component component = components[i];
                if (component == null)
                    continue;

                Type type = component.GetType();
                string typeName = type.Name;
                DetectFeatureFromTypeName(typeName, sourcePath);

                if (typeName.IndexOf("UniversalAdditionalCameraData", StringComparison.OrdinalIgnoreCase) >= 0 ||
                    typeName.IndexOf("UniversalAdditionalLightData", StringComparison.OrdinalIgnoreCase) >= 0)
                {
                    ScanSerializedObjectForFeatureEvidence(component, sourcePath);
                }
            }
        }

        private void ScanScriptsForRuntimeKeywords()
        {
            string[] guids = AssetDatabase.FindAssets("t:Script", SearchFolders());
            Regex keywordCallRegex = new Regex(@"\.(?:Enable|Disable|Set)Keyword\s*\(\s*""([A-Za-z0-9_]+)""", RegexOptions.Compiled);
            Regex localKeywordRegex = new Regex(@"new\s+LocalKeyword\s*\(\s*[^,]+,\s*""([A-Za-z0-9_]+)""\s*\)", RegexOptions.Compiled);
            Regex shaderKeywordRegex = new Regex(@"new\s+ShaderKeyword\s*\(\s*(?:[^,]+,\s*)?""([A-Za-z0-9_]+)""\s*\)", RegexOptions.Compiled);

            for (int i = 0; i < guids.Length; i++)
            {
                if (!Progress("Scanning scripts for runtime keywords", $"{i + 1}/{guids.Length}", i, guids.Length))
                    return;

                string path = AssetDatabase.GUIDToAssetPath(guids[i]);
                if (!ShouldScanAssetPath(path))
                    continue;

                string fullPath = Path.Combine(Directory.GetCurrentDirectory(), path);
                if (!File.Exists(fullPath))
                    continue;

                string text;
                try
                {
                    text = File.ReadAllText(fullPath);
                }
                catch
                {
                    continue;
                }

                int before = report.runtimeKeywordCalls.Count;
                RegisterRuntimeKeywordMatches(keywordCallRegex.Matches(text), path);
                RegisterRuntimeKeywordMatches(localKeywordRegex.Matches(text), path);
                RegisterRuntimeKeywordMatches(shaderKeywordRegex.Matches(text), path);

                if (report.runtimeKeywordCalls.Count > before)
                    report.runtimeKeywordSourcePaths.Add(path);
            }
        }

        private void RegisterRuntimeKeywordMatches(MatchCollection matches, string path)
        {
            foreach (Match match in matches)
            {
                string keyword = match.Groups[1].Value;
                if (string.IsNullOrEmpty(keyword))
                    continue;

                report.runtimeKeywordCalls.Add(keyword);
                DetectFeatureFromKeyword(keyword, "Runtime keyword reference: " + path);
            }
        }

        private void DetectFeatureFromKeyword(string keyword, string reason)
        {
            if (string.IsNullOrEmpty(keyword))
                return;

            foreach (FeatureRule rule in ANShaderVariantFeatureRuleDatabaseV7.Rules)
            {
                if (rule.keywords.Contains(keyword))
                {
                    MarkFeature(rule.id, reason + " | " + keyword);
                    return;
                }
            }
        }

        private void MarkFeature(string featureId, string reason)
        {
            report.detectedFeatureIds.Add(featureId);
            if (!string.IsNullOrEmpty(reason))
                report.detectedFeatures.Add(reason);
        }

        private void BuildRecommendations()
        {
            report.recommendations.Clear();
            report.recommendedStripKeywords.Clear();
            report.reviewKeywords.Clear();
            report.improvementNotes.Clear();

            HashSet<string> keywordEvidence = new HashSet<string>(report.materialKeywords, StringComparer.Ordinal);
            keywordEvidence.UnionWith(report.runtimeKeywordCalls);
            keywordEvidence.UnionWith(report.shaderGraphKeywordReferences);
            keywordEvidence.UnionWith(report.shaderVariantCollectionKeywords);
            keywordEvidence.UnionWith(report.vfxGraphKeywordReferences);
            keywordEvidence.UnionWith(report.renderGraphKeywordReferences);
            keywordEvidence.UnionWith(report.computeShaderKeywordReferences);
            keywordEvidence.UnionWith(report.addressableKeywords);
            keywordEvidence.UnionWith(report.assetBundleKeywords);

            foreach (FeatureRule rule in ANShaderVariantFeatureRuleDatabaseV7.Rules)
            {
                bool featureDetected = report.detectedFeatureIds.Contains(rule.id);
                bool keywordDetected = rule.keywords.Any(k => keywordEvidence.Contains(k));
                bool used = featureDetected || keywordDetected;

                Recommendation rec = new Recommendation { rule = rule, targetSummary = BuildTargetTagForRule(rule) };

                if (rule.id == "FOVEATED_RENDERING" && !used && report.detectedFeatureIds.Contains("XR_STEREO"))
                {
                    rec.action = RecommendationAction.Review;
                    rec.reason = "XR / stereo evidence was detected, so foveated rendering should be reviewed for this target instead of being automatically stripped.";
                    foreach (string keyword in rule.keywords)
                        report.reviewKeywords.Add(keyword);
                    report.improvementNotes.Add("Review foveated rendering because XR stereo evidence was detected for " + selectedBuildTarget + ".");
                    report.recommendations.Add(rec);
                    continue;
                }

                if (used)
                {
                    rec.action = RecommendationAction.Keep;
                    rec.reason = "Usage was detected from scene, material, pipeline, renderer feature or runtime keyword evidence.";
                    report.improvementNotes.Add("Keep " + rule.displayName + ": " + rule.improvement);
                }
                else
                {
                    bool allowStrip = rule.risk == StripRisk.Low ||
                                      (rule.risk == StripRisk.Medium && generateAggressiveCandidates) ||
                                      (rule.risk == StripRisk.Medium && IsGenerallySafeMediumRiskRule(rule));

                    if (allowStrip)
                    {
                        rec.action = RecommendationAction.StripCandidate;
                        rec.reason = "No usage evidence was detected by the scanner. This can be added to the generated strip-keyword profile after review.";
                        foreach (string keyword in rule.keywords)
                            report.recommendedStripKeywords.Add(keyword);

                        report.improvementNotes.Add("Strip candidate: " + rule.displayName + ". " + rule.improvement);
                    }
                    else
                    {
                        rec.action = RecommendationAction.Review;
                        rec.reason = "No usage evidence was detected, but this feature is risky to strip automatically. Review your URP Assets, runtime-loaded content, Addressables and AssetBundles before stripping.";
                        foreach (string keyword in rule.keywords)
                            report.reviewKeywords.Add(keyword);

                        report.improvementNotes.Add("Review before stripping: " + rule.displayName + ". " + rule.improvement);
                    }
                }

                report.recommendations.Add(rec);
            }

            ApplyRendererPathRecommendationAdjustments(keywordEvidence);
            CrossReferenceUrpBuiltInStripping();

            report.recommendedStripKeywords = report.recommendedStripKeywords
                .Where(k => !keywordEvidence.Contains(k))
                .Distinct(StringComparer.Ordinal)
                .OrderBy(k => k, StringComparer.Ordinal)
                .ToList();

            report.reviewKeywords = report.reviewKeywords
                .Where(k => !keywordEvidence.Contains(k))
                .Distinct(StringComparer.Ordinal)
                .OrderBy(k => k, StringComparer.Ordinal)
                .ToList();

            AddGeneralImprovementNotes();

            Log($"Recommendations generated. Strip keywords: {report.recommendedStripKeywords.Count}, review keywords: {report.reviewKeywords.Count}.");
            SaveLastReportToDisk();
            Repaint();
        }


        private void ApplyRendererPathRecommendationAdjustments(HashSet<string> keywordEvidence)
        {
            if (report.rendererDataEntries == null || report.rendererDataEntries.Count == 0)
                return;

            bool hasForward = report.rendererDataEntries.Any(e => e.renderingPath.IndexOf("Forward ", StringComparison.OrdinalIgnoreCase) >= 0 || e.renderingPath.StartsWith("Forward(", StringComparison.OrdinalIgnoreCase) || e.renderingPath.StartsWith("Forward (", StringComparison.OrdinalIgnoreCase));
            bool hasForwardPlus = report.rendererDataEntries.Any(e => e.renderingPath.IndexOf("Forward+", StringComparison.OrdinalIgnoreCase) >= 0);
            bool hasDeferred = report.rendererDataEntries.Any(e => e.renderingPath.IndexOf("Deferred", StringComparison.OrdinalIgnoreCase) >= 0);

            string summary = "Renderer paths detected: " + string.Join(", ", report.rendererDataEntries.Select(e => e.renderingPath).Distinct().OrderBy(s => s, StringComparer.Ordinal));

            Recommendation additionalLights = report.recommendations.FirstOrDefault(r => r.rule != null && r.rule.id == "ADDITIONAL_LIGHTS");
            if (additionalLights != null)
            {
                additionalLights.reason += "\n\n" + summary + ". Forward+ does not use the same per-vertex additional-light variant pressure as Forward.";
                if ((hasForwardPlus || hasDeferred) && !hasForward && !keywordEvidence.Contains("_ADDITIONAL_LIGHTS_VERTEX"))
                {
                    report.recommendedStripKeywords.Add("_ADDITIONAL_LIGHTS_VERTEX");
                    report.improvementNotes.Add("Renderer path adjustment: no Forward renderer path was detected, so _ADDITIONAL_LIGHTS_VERTEX is a stronger strip candidate than _ADDITIONAL_LIGHTS.");
                }
            }

            if (hasDeferred)
            {
                Recommendation additionalLightShadows = report.recommendations.FirstOrDefault(r => r.rule != null && r.rule.id == "ADDITIONAL_LIGHT_SHADOWS");
                if (additionalLightShadows != null)
                    additionalLightShadows.reason += "\n\nDeferred renderer data was detected. Review shadow variants against the deferred renderer path instead of applying Forward-only assumptions.";

                Recommendation renderPass = report.recommendations.FirstOrDefault(r => r.rule != null && r.rule.id == "RENDER_PASS");
                if (renderPass != null)
                    renderPass.reason += "\n\nDeferred/Deferred+ renderer paths can change native render-pass and G-buffer variant relevance.";
            }
        }

        private void CrossReferenceUrpBuiltInStripping()
        {
            report.urpBuiltInStripNotes.Clear();

            if (report.urpStripSettingsEntries == null || report.urpStripSettingsEntries.Count == 0)
                return;

            foreach (Recommendation rec in report.recommendations)
            {
                if (rec == null || rec.rule == null || rec.action != RecommendationAction.StripCandidate)
                    continue;

                List<string> notes = new List<string>();

                foreach (UrpStripSettingsEntry entry in report.urpStripSettingsEntries)
                {
                    if (entry.stripUnusedVariants)
                        notes.Add(Path.GetFileName(entry.assetPath) + ": URP Strip Unused Variants is enabled, so Unity may already strip unused feature states for " + rec.rule.displayName + ".");

                    if (entry.stripDebugVariants && (rec.rule.id == "DEBUG_DISPLAY" || rec.rule.id == "EDITOR_VISUALIZATION"))
                        notes.Add(Path.GetFileName(entry.assetPath) + ": URP Strip Debug Variants is enabled, so this recommendation is probably redundant.");

                    if (entry.stripUnusedPostProcessingVariants &&
                        (rec.rule.id == "POST_PROCESS_AA" || rec.rule.id == "SCREEN_SPACE_LENS_FLARE" || rec.rule.id == "MOTION_VECTORS" || rec.rule.id == "TEMPORAL_UPSCALING"))
                    {
                        notes.Add(Path.GetFileName(entry.assetPath) + ": URP Strip Unused Post-processing Variants is enabled; verify whether Unity already removes these post-processing candidates.");
                    }
                }

                if (notes.Count > 0)
                {
                    rec.unityBuiltInStripNote = string.Join("\n", notes.Distinct());
                    report.urpBuiltInStripNotes.Add(rec.rule.displayName + ": " + rec.unityBuiltInStripNote.Replace("\n", " | "));
                }
            }
        }

        private string BuildGraphicsApiRecommendationNote()
        {
            if (report.graphicsApiNames == null || report.graphicsApiNames.Count == 0)
                return "";

            List<string> notes = new List<string>(report.graphicsApiNotes);
            if (notes.Count == 0)
                return "Configured Graphics APIs: " + string.Join(", ", report.graphicsApiNames.OrderBy(s => s, StringComparer.Ordinal));

            return "Configured Graphics APIs: " + string.Join(", ", report.graphicsApiNames.OrderBy(s => s, StringComparer.Ordinal)) + "\n" + string.Join("\n", notes);
        }

