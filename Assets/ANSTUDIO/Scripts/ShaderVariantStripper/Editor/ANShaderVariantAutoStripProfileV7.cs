        private void ScanShaderGraphTextForKeywords(string assetPath)
        {
            string fullPath = Path.Combine(Directory.GetCurrentDirectory(), assetPath);
            if (!File.Exists(fullPath))
                return;

            string text;
            try
            {
                text = File.ReadAllText(fullPath);
            }
            catch
            {
                return;
            }

            bool foundAny = false;

            foreach (Match match in Regex.Matches(text, @"m_DefaultReferenceName:\s*([A-Za-z0-9_]+)"))
            {
                RegisterShaderGraphKeywordReference(match.Groups[1].Value, assetPath);
                foundAny = true;
            }

            foreach (Match match in Regex.Matches(text, @"""m_DefaultReferenceName""\s*:\s*""([A-Za-z0-9_]+)"""))
            {
                RegisterShaderGraphKeywordReference(match.Groups[1].Value, assetPath);
                foundAny = true;
            }

            if (foundAny)
                report.shaderGraphKeywordSourcePaths.Add(assetPath);
        }

