        private void ScanRenderPipelineGlobalSettings()
        {
            Type[] candidateTypes = AppDomain.CurrentDomain.GetAssemblies()
                .SelectMany(a =>
                {
                    try { return a.GetTypes(); }
                    catch { return new Type[0]; }
                })
                .Where(t => t != null && (
                    string.Equals(t.FullName, "UnityEngine.Rendering.Universal.UniversalRenderPipelineGlobalSettings", StringComparison.Ordinal) ||
                    string.Equals(t.Name, "HDRenderPipelineGlobalSettings", StringComparison.Ordinal) ||
                    string.Equals(t.Name, "RenderPipelineGlobalSettings", StringComparison.Ordinal)))
                .Distinct()
                .ToArray();

            MethodInfo genericMethod = typeof(GraphicsSettings).GetMethods(BindingFlags.Public | BindingFlags.Static)
                .FirstOrDefault(m => m.Name == "GetRenderPipelineSettings" && m.IsGenericMethodDefinition && m.GetParameters().Length == 0);

            if (genericMethod == null)
            {
                Log("GraphicsSettings.GetRenderPipelineSettings<T>() was not found in this Unity version.");
                return;
            }

            foreach (Type type in candidateTypes)
            {
                try
                {
                    MethodInfo method = genericMethod.MakeGenericMethod(type);
                    UnityEngine.Object settings = method.Invoke(null, null) as UnityEngine.Object;
                    if (settings == null)
                        continue;

                    string path = AssetDatabase.GetAssetPath(settings);
                    if (string.IsNullOrEmpty(path))
                        path = settings.name + " (RenderPipelineGlobalSettings)";

                    report.globalSettingsPaths.Add(type.Name + " | " + path);
                    ScanSerializedObjectForFeatureEvidence(settings, path);
                    ScanTextAssetForKeywordReferences(path, report.renderGraphKeywordReferences, report.renderGraphKeywordSourcePaths, "RenderPipelineGlobalSettings keyword");
                }
                catch (Exception ex)
                {
                    Log("RenderPipelineGlobalSettings scan skipped " + type.Name + ": " + ex.Message);
                }
            }
        }

        private void ScanTextAssetForKeywordReferences(string assetPath, HashSet<string> target, HashSet<string> sourcePaths, string sourceLabel)
        {
            string text = TryReadProjectRelativeText(assetPath);
            if (string.IsNullOrEmpty(text))
                return;

            int before = target.Count;
            RegisterKeywordLikeTokensFromText(text, target, sourceLabel + ": " + assetPath);
            if (target.Count > before && sourcePaths != null)
                sourcePaths.Add(assetPath);
        }

        private void RegisterKeywordLikeTokensFromText(string text, HashSet<string> target, string reason)
        {
            if (string.IsNullOrEmpty(text) || target == null)
                return;

            foreach (Match match in Regex.Matches(text, "(?:ShaderKeyword|LocalKeyword|shaderKeyword|keyword|keywords|m_DefaultReferenceName|referenceName)[^\\n\\r\\\"']{0,160}[\\\"']([A-Za-z0-9_]+)[\\\"']", RegexOptions.IgnoreCase))
                RegisterEvidenceKeyword(match.Groups[1].Value, target, reason);

            foreach (Match match in Regex.Matches(text, @"\b_?[A-Z][A-Z0-9_]{2,}\b"))
                RegisterEvidenceKeyword(match.Value, target, reason);
        }

        private void RegisterEvidenceKeyword(string keyword, HashSet<string> target, string reason)
        {
            if (!LooksLikeShaderKeywordToken(keyword))
                return;

            target.Add(keyword);
            DetectFeatureFromKeyword(keyword, reason);
        }

        private bool LooksLikeShaderKeywordToken(string keyword)
        {
            if (string.IsNullOrWhiteSpace(keyword))
                return false;

            keyword = keyword.Trim();
            if (!Regex.IsMatch(keyword, "^[A-Za-z0-9_]+$"))
                return false;

            if (keyword.Length < 3 || keyword.Length > 80)
                return false;

            string lower = keyword.ToLowerInvariant();
            if (lower == "true" || lower == "false" || lower == "guid" || lower == "fileid" || lower == "mono" || lower == "script")
                return false;

            bool hasUpper = keyword.Any(char.IsUpper);
            bool hasUnderscore = keyword.IndexOf('_') >= 0;
            bool known = ANShaderVariantFeatureRuleDatabaseV7.Rules.Any(r => r.keywords != null && r.keywords.Contains(keyword));
            return known || (hasUpper && hasUnderscore) || keyword.StartsWith("PROBE_VOLUMES", StringComparison.Ordinal) || keyword.StartsWith("FOVEATED_", StringComparison.Ordinal);
        }

        private string TryReadProjectRelativeText(string assetPath)
        {
            if (string.IsNullOrEmpty(assetPath))
                return "";

            string normalized = assetPath.Replace('\\', '/');
            if (!(normalized.StartsWith("Assets/", StringComparison.OrdinalIgnoreCase) || normalized.StartsWith("Packages/", StringComparison.OrdinalIgnoreCase) || normalized.StartsWith("ProjectSettings/", StringComparison.OrdinalIgnoreCase)))
                return "";

            string fullPath = Path.Combine(Directory.GetCurrentDirectory(), normalized);
            if (!File.Exists(fullPath))
                return "";

            try
            {
                return File.ReadAllText(fullPath, Encoding.UTF8);
            }
            catch
            {
                try { return File.ReadAllText(fullPath); }
                catch { return ""; }
            }
        }

        private void ScanScenes()
        {
            List<string> scenePaths = GetScenePathsToScan();

            if (openScenesForComponentScan && scenePaths.Count > 0)
            {
                bool canProceed = EditorSceneManager.SaveCurrentModifiedScenesIfUserWantsTo();
                if (!canProceed)
                {
                    cancelRequested = true;
                    Log("Scene scan cancelled because current modified scenes were not saved.");
                    return;
                }
            }

            for (int i = 0; i < scenePaths.Count; i++)
            {
                string scenePath = scenePaths[i];

                if (!Progress("Scanning scenes", $"{i + 1}/{scenePaths.Count}: {scenePath}", i, scenePaths.Count))
                    return;

                if (!ShouldScanAssetPath(scenePath))
                    continue;

                ScanSceneDependencies(scenePath);

                if (!openScenesForComponentScan)
                {
                    report.scannedScenes.Add(scenePath + " (dependencies only)");
                    continue;
                }

                if (ShouldSkipSceneOpen(scenePath, out string reason))
                {
                    report.skippedScenes.Add(scenePath + " - " + reason);
                    Log("Skipped scene open: " + scenePath + " - " + reason);
                    continue;
                }

                OpenAndScanScene(scenePath);
            }
        }

        private List<string> GetScenePathsToScan()
        {
            List<string> paths = new List<string>();

            if (scanOnlyEnabledBuildScenes)
            {
                foreach (EditorBuildSettingsScene scene in EditorBuildSettings.scenes)
                {
                    if (scene.enabled && !string.IsNullOrEmpty(scene.path))
                        paths.Add(scene.path);
                }
            }
            else
            {
                string[] guids = AssetDatabase.FindAssets("t:Scene", SearchFolders());
                foreach (string guid in guids)
                {
                    string path = AssetDatabase.GUIDToAssetPath(guid);
                    if (!string.IsNullOrEmpty(path))
                        paths.Add(path);
                }
            }

            return paths.Distinct().OrderBy(p => p, StringComparer.OrdinalIgnoreCase).ToList();
        }

        private void ScanSceneDependencies(string scenePath)
        {
            string[] deps = AssetDatabase.GetDependencies(scenePath, true);
            foreach (string dep in deps)
            {
                if (!ShouldScanAssetPath(dep))
                    continue;

                Material mat = AssetDatabase.LoadAssetAtPath<Material>(dep);
                if (mat != null)
                {
                    ScanMaterial(mat, dep);
                    continue;
                }

                RenderPipelineAsset rp = AssetDatabase.LoadAssetAtPath<RenderPipelineAsset>(dep);
                if (rp != null)
                    ScanRenderPipelineAsset(rp, dep);

                ScriptableObject so = AssetDatabase.LoadAssetAtPath<ScriptableObject>(dep);
                if (so != null)
                    ScanScriptableObjectForRendererFeatures(so, dep);
            }
        }

        private bool ShouldSkipSceneOpen(string scenePath, out string reason)
        {
            reason = "";

            string normalized = scenePath.Replace('\\', '/');

            if (skipReadOnlyPackageScenes && normalized.StartsWith("Packages/", StringComparison.OrdinalIgnoreCase))
            {
                reason = "package scene";
                return true;
            }

            if (skipReadOnlyPackageScenes)
            {
                try
                {
                    UnityEditor.PackageManager.PackageInfo packageInfo =
                        UnityEditor.PackageManager.PackageInfo.FindForAssetPath(normalized);

                    if (packageInfo != null && !normalized.StartsWith("Assets/", StringComparison.OrdinalIgnoreCase))
                    {
                        reason = "read-only package asset";
                        return true;
                    }
                }
                catch
                {
                    // If PackageInfo lookup fails, fall back to IsOpenForEdit below.
                }

                try
                {
                    string message;
                    bool openForEdit = AssetDatabase.IsOpenForEdit(normalized, out message);
                    if (!openForEdit && !string.IsNullOrEmpty(message))
                    {
                        reason = message;
                        return true;
                    }
                }
                catch
                {
                    // IsOpenForEdit can fail on unusual paths. Do not block local assets based on this alone.
                }
            }

            return false;
        }

        private void OpenAndScanScene(string scenePath)
        {
            Scene scene = default;
            bool opened = false;

            try
            {
                scene = EditorSceneManager.OpenScene(scenePath, OpenSceneMode.Additive);
                opened = scene.IsValid();
                if (!opened)
                {
                    report.skippedScenes.Add(scenePath + " - failed to open");
                    return;
                }

                report.scannedScenes.Add(scenePath);

                GameObject[] roots = scene.GetRootGameObjects();
                int rootCount = Mathf.Max(roots.Length, 1);

                for (int rootIndex = 0; rootIndex < roots.Length; rootIndex++)
                {
                    GameObject root = roots[rootIndex];
                    if (cancelRequested)
                        break;

                    float rootBase = rootIndex / (float)rootCount;
                    float rootSpan = 1f / rootCount;

                    Renderer[] renderers = root.GetComponentsInChildren<Renderer>(true);
                    for (int r = 0; r < renderers.Length; r++)
                    {
                        if ((r & 15) == 0 && !ProgressValue("Scanning scene renderers", $"{scenePath} | root {rootIndex + 1}/{roots.Length} | renderer {r + 1}/{renderers.Length}", rootBase + rootSpan * 0.33f * (renderers.Length <= 0 ? 1f : r / (float)renderers.Length)))
                            return;

                        ScanRenderer(renderers[r], scenePath);
                    }

                    Light[] lights = root.GetComponentsInChildren<Light>(true);
                    for (int l = 0; l < lights.Length; l++)
                    {
                        if ((l & 7) == 0 && !ProgressValue("Scanning scene lights", $"{scenePath} | root {rootIndex + 1}/{roots.Length} | light {l + 1}/{lights.Length}", rootBase + rootSpan * (0.33f + 0.17f * (lights.Length <= 0 ? 1f : l / (float)lights.Length))))
                            return;

                        ScanLight(lights[l], scenePath);
                    }

                    ReflectionProbe[] probes = root.GetComponentsInChildren<ReflectionProbe>(true);
                    if (probes.Length > 0)
                        MarkFeature("REFLECTION_PROBES", $"Scene has ReflectionProbe: {scenePath}");

                    LODGroup[] lodGroups = root.GetComponentsInChildren<LODGroup>(true);
                    if (lodGroups.Length > 0)
                        MarkFeature("LOD_CROSSFADE", $"Scene has LODGroup: {scenePath}");

                    Terrain[] terrains = root.GetComponentsInChildren<Terrain>(true);
                    if (terrains.Length > 0)
                        report.detectedFeatures.Add("Terrain");

                    Component[] components = root.GetComponentsInChildren<Component>(true);
                    ScanComponents(components, scenePath, rootBase + rootSpan * 0.50f, rootSpan * 0.50f, scenePath);
                }

                if (RenderSettings.fog)
                    MarkFeature("FOG", "RenderSettings.fog is enabled while scanning " + scenePath);

                if (LightmapSettings.lightmaps != null && LightmapSettings.lightmaps.Length > 0)
                    MarkFeature("LIGHTMAPS", "LightmapSettings contains baked lightmaps while scanning " + scenePath);
            }
            catch (Exception ex)
            {
                report.skippedScenes.Add(scenePath + " - exception: " + ex.Message);
                Log("Scene scan exception: " + scenePath + " - " + ex.Message);
            }
            finally
            {
                if (opened && scene.IsValid())
                {
                    try
                    {
                        EditorSceneManager.CloseScene(scene, true);
                    }
                    catch (Exception closeEx)
                    {
                        Log("Failed to close scanned scene: " + scenePath + " - " + closeEx.Message);
                    }
                }
            }
        }

        private void ScanPipelineAssets()
        {
            RenderPipelineAsset defaultAsset = GraphicsSettings.defaultRenderPipeline;
            if (defaultAsset != null)
            {
                string path = AssetDatabase.GetAssetPath(defaultAsset);
                ScanRenderPipelineAsset(defaultAsset, path);
            }

            RenderPipelineAsset activeQualityAsset = QualitySettings.renderPipeline;
            if (activeQualityAsset != null)
            {
                string path = AssetDatabase.GetAssetPath(activeQualityAsset);
                ScanRenderPipelineAsset(activeQualityAsset, path);
            }

            ScanQualityPipelineAssetsByReflection();

            string[] guids = AssetDatabase.FindAssets("t:RenderPipelineAsset", SearchFolders());
            for (int i = 0; i < guids.Length; i++)
            {
                if (!Progress("Scanning render pipeline assets", $"{i + 1}/{guids.Length}", i, guids.Length))
                    return;

                string path = AssetDatabase.GUIDToAssetPath(guids[i]);
                if (!ShouldScanAssetPath(path))
                    continue;

                RenderPipelineAsset asset = AssetDatabase.LoadAssetAtPath<RenderPipelineAsset>(path);
                ScanRenderPipelineAsset(asset, path);
            }
        }

        private void ScanQualityPipelineAssetsByReflection()
        {
            try
            {
                System.Reflection.MethodInfo method = typeof(QualitySettings).GetMethod("GetRenderPipelineAssetAt", System.Reflection.BindingFlags.Public | System.Reflection.BindingFlags.Static);
                if (method == null)
                    return;

                string[] names = QualitySettings.names;
                for (int i = 0; i < names.Length; i++)
                {
                    object value = method.Invoke(null, new object[] { i });
                    RenderPipelineAsset asset = value as RenderPipelineAsset;
                    if (asset == null)
                        continue;

                    string path = AssetDatabase.GetAssetPath(asset);
                    ScanRenderPipelineAsset(asset, path);
                }
            }
            catch (Exception ex)
            {
                Log("Quality pipeline reflection scan failed: " + ex.Message);
            }
        }

        private void ScanRenderPipelineAsset(RenderPipelineAsset asset, string path)
        {
            if (asset == null)
                return;

            if (string.IsNullOrEmpty(path))
                path = AssetDatabase.GetAssetPath(asset);

            if (!string.IsNullOrEmpty(path))
                report.pipelineAssetPaths.Add(path);

            ScanSerializedObjectForFeatureEvidence(asset, path);
            ScanUrpBuiltInStripSettings(asset, path);

            if (!string.IsNullOrEmpty(path))
            {
                string[] deps = AssetDatabase.GetDependencies(path, true);
                foreach (string dep in deps)
                {
                    if (!ShouldScanAssetPath(dep))
                        continue;

                    ScriptableObject so = AssetDatabase.LoadAssetAtPath<ScriptableObject>(dep);
                    if (so != null)
                        ScanScriptableObjectForRendererFeatures(so, dep);
                }
            }
        }

        private void ScanScriptableObjectForRendererFeatures(ScriptableObject so, string path)
        {
            if (so == null)
                return;

            Type type = so.GetType();
            string typeName = type.Name;
            string fullName = type.FullName ?? typeName;

            bool isRendererData = typeName.IndexOf("RendererData", StringComparison.OrdinalIgnoreCase) >= 0 ||
                                  fullName.IndexOf("RendererData", StringComparison.OrdinalIgnoreCase) >= 0 ||
                                  typeName.IndexOf("UniversalRendererData", StringComparison.OrdinalIgnoreCase) >= 0 ||
                                  fullName.IndexOf("UniversalRendererData", StringComparison.OrdinalIgnoreCase) >= 0;

            if (typeName.IndexOf("RendererFeature", StringComparison.OrdinalIgnoreCase) >= 0 ||
                fullName.IndexOf("RendererFeature", StringComparison.OrdinalIgnoreCase) >= 0 ||
                isRendererData)
            {
                report.rendererFeatures.Add($"{typeName} | {path}");
            }

            if (isRendererData)
                ScanRendererDataInventory(so, path);

            DetectFeatureFromTypeName(typeName, path);
            ScanSerializedObjectForFeatureEvidence(so, path);
        }

        private void ScanSerializedObjectForFeatureEvidence(UnityEngine.Object obj, string path)
        {
            if (obj == null)
                return;

            try
            {
                SerializedObject serializedObject = new SerializedObject(obj);
                SerializedProperty iterator = serializedObject.GetIterator();

                int guard = 0;
                while (iterator.NextVisible(true))
                {
                    guard++;
                    if (guard > 2000)
                        break;

                    string propName = iterator.name ?? "";
                    string displayName = iterator.displayName ?? "";
                    string combined = propName + " " + displayName;

                    if (iterator.propertyType == SerializedPropertyType.Boolean && iterator.boolValue)
                    {
                        DetectFeatureFromProperty(combined, path);
                    }
                    else if (iterator.propertyType == SerializedPropertyType.Integer && iterator.intValue != 0)
                    {
                        DetectFeatureFromProperty(combined, path);
                    }
                    else if (iterator.propertyType == SerializedPropertyType.ObjectReference && iterator.objectReferenceValue != null)
                    {
                        DetectFeatureFromTypeName(iterator.objectReferenceValue.GetType().Name, path);
                    }
                    else if (iterator.propertyType == SerializedPropertyType.String && !string.IsNullOrEmpty(iterator.stringValue))
                    {
                        DetectFeatureFromString(iterator.stringValue, path);
                    }
                }
            }
            catch
            {
                // Some editor objects do not serialize cleanly. They are ignored.
            }
        }

